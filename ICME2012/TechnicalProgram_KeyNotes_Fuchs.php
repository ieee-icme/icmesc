<p><i>
Henry Fuchs has been active in computer graphics since the 1970s, with rendering algorithms (BSP Trees), hardware (Pixel-Planes), virtual environments, tele-immersion systems and medical applications. He is a member of the (US) National Academy of Engineering, the American Academy of Arts and Sciences, recipient of the 1992 ACM-SIGGRAPH Achievement Award, the 1992 Academic Award of the National Computer Graphics Association, and the 1997 Satava Award of the Medicine Meets Virtual Reality Conferences. With Nadia Thalmann and Markus Gross, he co-directs the NTU Singapore - ETH Zurich - UNC Chapel Hill BeingThere Telepresence Research Centre.
