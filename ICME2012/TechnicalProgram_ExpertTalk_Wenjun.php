<strong>ICME2012 Expert Talk</strong><br/><br/>
Title: <strong><u>"High Order Entropy Coding - From Conventional Video Coding to Distributed Video Coding"</u></strong><br/><br/>

<i>
High order entropy coding has been extensively studied for conventional/centralized image/video coding and is believed to be much more important for improving the coding efficiency than adapting transform and quantization to the input signal [1-4]. Yet it has not been explored to any significant extent for distributed video coding (DVC), a paradigm shifting approach that features “simple encoder and complex decoder” that is well suited to emerging applications such as wireless sensor network and distributed parallel processing.
<br/><br/>
 DVC research in the past decade has shown significant performance gap from conventional video coding techniques despite many advantages of the DVC paradigm. This is mainly because DVC suffers from the extreme difficulty in estimating the side information (equivalent to the motion compensated prediction in conventional video coding). This major obstacle has led to confusion and misconception, which has discouraged researchers to look into the issue of exploiting high order spatial correlations in DVC - a task itself proving to be very challenging too in the DVC paradigm. Recent work in my group [5] provided some theoretical analysis of the performance of DVC in terms of side information estimation and has demonstrated that in practice it has comparable performance as traditional motion compensated prediction. This suggests that it is the right time now to move on to investigate how to efficiently explore the high order spatial correlations in DVC. In this talk, I will review the evolution of techniques that have been proposed for high order entropy coding in conventional video coding, with a focus on high order context based approaches, and discuss how previous ideas and experiences can be leveraged to speed-up the progress in designing highly efficient entropy coding in the context of DVC.
<br/><br/>
