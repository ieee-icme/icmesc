	    The past decade witnesses the emergence and popularity of online social websites, blogs, instant messaging systems, and media-sharing networks, where millions of people actively interact with each other and expand their social circles. Such social networks enable users to share information and experiences, and organize communities and activities. They bring new experience to users, and profoundly change the way we create, share and consume multimedia content. These trends also pose new challenges to the effective management of the resulting avalanche of multimedia content that people create, share and distribute over networks. In addition, users demand personalized services to meet their individual needs. These trends also raise new challenges related to security and privacy issues.<br/><br/> 
	    These challenges call for novel solutions to develop signal processing technology with consideration of social content, to analyze the impact of human factors on multimedia networks, and to design multimedia systems with enhanced performance. This is an interdisciplinary research area covering multimedia signal processing, social signal processing, information science, sociology, psychology, and economics. The proposed summer school aims to provide a comprehensive overview of recent advances in this emerging research field, to provide graduate students, early stage researchers and practitioners a platform to interact with international and local researchers, and to engage local chapters in membership development and educational activities. <br/><br/>
	    This seasonal school will cover the following topics in social media processing:<br/>&nbsp;&nbsp;1) multimedia social network analysis and user behavior modeling;<br/>&nbsp;&nbsp;2) multimedia data mining and retrieval in social context;<br/>&nbsp;&nbsp;3) multimedia communication and networking in social context;<br/>&nbsp;&nbsp;4) trust, privacy and security in social networks.<br/><br/> </p></td>
	    
             
            
