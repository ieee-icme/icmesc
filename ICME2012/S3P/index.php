	    <img src="../pic/th_new_icon.gif" border="0"> <a href="http://en.wikipedia.org/wiki/Web_conferencing" target="_blank" class="styleWithUnderline"><font color="#FF0000">Online webinar</font></a> will be established for those participants who cannot travel to Sydney.<br/><br/>
	    
