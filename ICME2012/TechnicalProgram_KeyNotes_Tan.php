<p>  <i> <strong>Haptics and its Application in Multimodal User Interfaces</strong>
</i></p>

<p>  <i> <strong>Abstract:</strong>
</i></p>
<p><i>For a long time, the sense of touch has been regarded as an inferior sense as compared to vision or audition.  However, the potential to receive information through touch is well illustrated by natural communication methods used by individuals with severe auditory and/or visual impairments.  The last decade has witnessed renewed interests in transmitting information through touch for enhanced interaction experience.  I will start this research overview talk will a brief introduction to the human somatosensory system. I will then present an overview of state-of-the-art haptic technologies. Finally, I will discuss two research programs, haptic cuing of visual attention and visuohaptic watermarking, to illustrate the application of haptics in multimodal user interfaces.
    
</i></p>

<p><i><strong>About the Speaker:</strong>
</i></p>
<p><i>Hong Z. Tan is a professor of electrical and computer engineering with courtesy appointments in mechanical engineering and psychological sciences at Purdue University. She is currently working at Microsoft Research Asia while taking a research leave from Purdue University. Her research focuses on haptic human-machine interfaces and haptic perception. She is best known for her perception-based approach to solving engineering problems. She received her Bachelor's degree in Biomedical Engineering from Shanghai Jiao Tong University, P.R. China. She earned her Master and Doctorate degrees, both in Electrical Engineering and Computer Science, from the Massachusetts Institute of Technology (MIT). She was a Research Scientist at the MIT Media Laboratory before joining the faculty at Purdue's School of Electrical and Computer Engineering in 1998. She has held a McDonnell Visiting Fellowship at Oxford University, and a Visiting Associate Professorship in the Department of Computer Science at Stanford University. 
</i></p>
<p><i> Tan was a recipient of the US National Science Foundation's Early Faculty Development (CAREER) Award from 2000 to 2004. In addition to serving on numerous program committees, she was a co-organizer (with Blake Hannaford) of the International Symposium on Haptic Interfaces for Virtual Environment and Teleoperator Systems from 2003 to 2005. She was the founding chair of the IEEE Technical Committee on Haptics, a home for the international interdisciplinary haptics research community, from 2006 to 2008.  She has served as an associate editor of Presence: Teleoperators & Virtual Environments, ACM Transactions on Applied Perception and IEEE Transactions on Haptics.
  </i>
  
