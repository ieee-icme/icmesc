<p> <strong>OpenGL SC Implementation over an OpenGL ES 1.1 Graphics Board </strong>
</p>


<p>Abstract: OpenGL SC, the safety critical profile of OpenGL plays the major role for the graphical user interfaces, especially in the safety-critical markets, including avionics, military, medical and automotive applications. In other side, OpenGL ES, the embedded systems version of OpenGL, has many commercial implementations. In this demonstration, we show that the OpenGL SC features can be provided over the widespread OpenGL ES graphics boards. This is the most costeffective way of implementing OpenGL SC, at this time. Our result is the first implementation based on OpenGL ES 1.1 hardware. We will demonstrate this OpenGL SC-over-OpenGL ES 1.1 implementation, and show its successful behaviors.
</p>

