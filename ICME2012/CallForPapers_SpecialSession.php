                    <em>Online multimedia content has grown to take up a significant portion   of all bits being exchanged. Notably, videos account for 37% of peak   hour web traffic in 2010, and this is still poised to grow to over 50%   in the next two years. Online social network,   on the other hand, has become the largest virtual meeting places. With   91% online adults access social media in a typical month, it has become a   platform for sharing experiences, germinating ideas, as well as   building and maintaining relationships. A large   share of messages being communicated on social media platforms contains   or points to multimedia content. Up to 60% microblog messages contain   (link) to a picture or video, on one of China¡¯s largest microblogging   platform. <br />
                    <br />
This session is organized around two main themes where multimedia meets   social networks. On one hand community--©\based intelligence will help   tackle some of the most challenging problems in artificially   intelligence and multimedia understanding, such as using   folksonomy tags and user comments to understand the meanings of an   image or an video. On the other hand, tracking multimedia messages as   they propagate through the social web will help understand the structure   and dynamics of online information network at   large. There are exciting recent development in both areas. We aim to   bring together novel recent work on social media analysis and online   information flow, each of our invited papers cover unique aspects of the   problem space. We believe this problem area   and will generate considerable interest, and we hope to share these   exciting developments with the ICME community. <br />
<br />
This special session will focus on the synergy between multimedia   content and online social networks. Although social media research can   be traced through at least 1960s with? Milgram¡¯s famous small--©\world   experiment, our collective understanding of online   social mechanisms is still far from complete. Especially in the context   of massive online traces? around social media content, and in the   abundance of multimedia messages that gained popularity only in the last   decade or so.We aim to create a collegial forum   to present current work and exchange ideas, we will also brain storm as   community on a few key questions: the main challenges and techniques   relevant for this area, how the research area will evolve within and   across several existing communities, and its projected   in impact in the next few years.<br />
<br />
In particular, we have invited leading researchers from both industry   and academia, their work covers the full range of problem aspects in   social media: from media content analysis, multimedia information   diffusion, recommendation and annotation, location--©\sensitive   social media tracking, touser behavioral modeling.<br />
