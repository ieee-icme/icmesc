                <td colspan="3"><div align="justify" class="STYLE2"><strong><a name="Delivery" id="Delivery">Multimedia Delivery over 4G Wireless Broadband Cellular Networks</a></strong><br />
                    <em>Proliferation of video capable mobile devices with comfortable screen   resolutions such as smart phones and tablets is gradually moving   internet users towards full mobility. In parallel, the necessity of   accessing and sharing video content anytime and anywhere   generated by enormous spread of social networking and video content   sharing applications is driving increased consumer requirements for   broadband cellular wireless access. This trend will further evolve with   the integration of video streaming and social networking   into novel set of personalized video-enabled social interactions among   mobile users through applications such as mobile video blogging,   ¡®see-what-i-see¡¯ and user-to-user content sharing. The expected   explosion of multimedia services available for mobile users   including user-generated content will make multimedia traffic dominant   in future mobile broadband systems. To this end, fourth generation (4G)   cellular standards such as Long Term Evolution - Advanced (LTE-A) or   further evolutions of IEEE WiMaX standard are   planned to meet the expected surge in wireless video data rate   requirements.<br />
                    <br />
LTE (Release 8) and LTE-A (Release 10) are next generation cellular   communication systems promising significantly higher data rates with   downlink peak data rates up to 100 Mb/s and 1000 Mb/s for LTE and LTE-A,   respectively. In addition, a number of improvements   including higher average/cell-edge throughput and improved spectral   efficiency are envisaged with the introduction of new technologies.   Meeting the aggressive targets of LTE and LTE-A require adopting a set   of advanced communication techniques such as orthogonal   frequency division multiplexing/multiple access (OFDMA/OFDMA),   multiple-input multiple-output (MIMO), adaptive modulation and coding   (AMC), coordinated multi-point (CoMP), multi-hop and cooperative   relaying transmission, etc. The technological advances achieved   with LTE/LTE-A system dramatically increased per user available   throughput and made comfortable multimedia services over cellular   infrastructure a reality.<br />
<br />
Despite the possibilities offered by introduction of new technologies,   there is still a lot of work to be done towards achieving a goal of   seamless and efficient video delivery over cellular networks. Novel   trends in mobile video delivery demand additional   efforts with respect to assurance of Quality of Experience (QoE)   requested by the end-user and the price in terms of energy consumption   that has to be paid for expected increase in mobile video service users.   Apart from QoE and energy awareness, a number of   additional technical problems related to efficient video delivery, such   as rate adaptation, scheduling and resource allocation, design of   unicast and multicast/broadcast services, forward error correction,   etc., need to be addressed in detail.<br />
<br />
The goal of the proposed special session is to present novel and   unpublished results that address challenges of video transmission over   4G wireless broadband cellular networks. The session will offer an   opportunity to researchers working in different research   areas such as video coding/processing, error correction coding,   WiMax/LTE transmission systems, wireless video delivery systems,   multimedia applications, etc, to get an overview of the problem and   current research efforts. The topic should be of considerable   interest to researchers working on video delivery over other broadband   mobile wireless systems such as wireless broadcast networks (DVB) or   wireless local area networks (WLAN).</em><br />
