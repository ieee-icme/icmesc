                  <strong><a name="Perceptual" id="Perceptual">Perceptual Processing of Digital Pictures</a></strong><br />
                    <em>Digital picture processing encompasses digital processing of both   still images and video or moving/motion pictures or image sequences for   visual communications and a wide range of applications. Since the human   being is the ultimate consumer of most (if not   all) digital pictures, it is logical and desirable to formulate,   design, optimize, implement, and test algorithms and systems of visual   signal processing with appropriate perceptual criteria for various   applications and services. Different perception based   models have been explored in many theoretical and practical aspects,   toward more effective and efficient visual signal acquisition,   compression, restoration, enhancement, summarization, delivery,   visualization, protection, and so on. As a result of evolution,   human perception has developed unique characteristics that can be   turned into advantages of relevant system design and development, for   resource saving and/or performance boosting. This calls for more   in©\depth research to achieve this challenging but meaningful   aim. <br />
                      <br />
                    This special session is intended to provide a forum for researchers and   practitioners to present their latest accomplishments and share their   experience on all aspects of perceptually driven digital picture   processing and communications. Topics of interest   include, but are not limited to, the following:</em>
                  
                  <br />
                      - Perceptual image/video coding<br />
                      - Pre©\ and post©\ processing techniques to enhance viewing experience<br />
                      - User©\oriented streaming, adaptation and retargeting<br />
                      - Perceptually optimized graphics rendering and display<br />
                      - Perception©\based visual signal retrieval<br />
                      - Perceptual watermarking and encryption<br />
                      - Perceptually optimized network video<br />
                      - Computational models of consumer/mobile perception<br />
                      - 3©\D/multiview video coding<br />
                      - Related applications, databases and standardization</em><br />
                      <br />
                    Organizers:<br />
