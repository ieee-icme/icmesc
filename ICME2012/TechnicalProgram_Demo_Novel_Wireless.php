<p> <strong> Novel Wireless Routers for Seamless Sharing of Video Access in Multihop Networks</strong>
</p>


<p>Abstract: This demonstration focuses on seamless video delivery over Wireless Mesh Networks with Dynamic Stream Merging (DSM) capability. The overall setup consists of a prototype with seven nodes forming the mesh network backbone. All these nodes run DSM software with mobility support. DSM is a robust technique that can reduce both gateway load and wireless traffic for video applications. It has the intelligence to substantially increase the service throughput. In DSM, the upstream nodes of the multicast streaming tree merges similar streams before sending the streams to the downstream nodes thus relaxing the stress in the gateway. The mobility support is provided by our novel session handoff technique. This technique uses a special tree searching algorithm for identifying the next mesh node (foreign node) for re-association with client and uses a novel session rate prediction technique for determining the rate at which the video has to be delivered by the foreign node.We believe that this prototype is the first prototype that can handle completely distributed session handoff over wireless mesh access networks.
</p>

