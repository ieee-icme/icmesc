 <a name="Learning and Mining with Visual Data on the Web"></a> 
		  Title: <u><strong>"Learning and Mining with Visual Data on the Web"</strong></u><br/><br/>
		  <i>Increasingly rich and large-scale image related data are being posted to social network and media sharing websites. Researchers from multidisciplinary areas, including machine learning, computer vision, data mining, and human machine interaction, are developing methods for various applications. This tutorial provides an overview of representative recent advances in this arena of opportunities and challenges. <br/><br/>

First, we discuss Web 2.0 which gave rise to the enormous amount of visual data on the Web. Next, we describe a new computational machinery, Patch Alignment Framework (PAF), which was proposed to unifying various manifold learning based dimension reduction algorithms. Second, we review the recently popular data driven approaches that extends PAF for various settings, such as non-negative data analytics, sparse learning, multiview learning, transfer learning and active learning. Finally, we give example applications of PAF and its variants.
<br/><br/>
