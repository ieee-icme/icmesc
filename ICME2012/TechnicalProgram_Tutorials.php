<a name="Human action analysis with 2D and 3D sensors"></a> 
		  Title: <u><strong>"Human action analysis with 2D and 3D sensors"</strong></u><br/><br/>
		  <i>Human action analysis is a critical task and emerging topic in many multimedia applications. In the past few years, there has been a lot of progress in action recognition with conventional 2D video cameras. Effective techniques have been developed to address many challenging issues in real world environments such as dynamic and cluttered background and occlusions. More recently, the availability of commodity depth cameras has brought a new level of excitement to this field. Rapid progress has been made that addresses new technical issues in action recognition with 3D depth cameras. In this tutorial, we introduce the basics for human action analysis, using both regular and depth cameras. The topics cover the action analysis using depth cameras, action and abnormal event detection in surveillance videos, as well as action analysis in user-generated consumer videos, such as movies and Youtube videos.<br/><br/>
