		<a name="Network Coding for Efficient Multimedia Content Delivery"></a> 
		 Title: <u><strong>"Network Coding for Efficient Multimedia Content Delivery"</strong></u><br/><br/>
		  <i>With the increasing popularity of multimedia content such as ultra-high definition video, multi-view video, free viewpoint video etc., it is a challenge for network service providers to distribute such high volume content at a high throughput while maintaining the required standard of quality of service. Popular internet applications such as live streaming, IP TV, web conferencing, etc. require delivering high volume multimedia content among multiple receivers. The usage of multicast technologies enables to deliver content to multiple receivers much more efficiently compared to unicast, albeit the question arises, are network resources utilized optimally? <br /><br />

Network Coding is a novel concept of network coding to optimally utilize network bandwidth. This treats information transmitted in a multicast network quite distinctively to the notion of regarding information as fluids. In network coding, information packets are coded at intermediate nodes. This increases the throughput at which information is delivered to receivers in a multicast network and improves the robustness against packet errors and losses. Due to such advantages, it is appealing to utilize network coding in practical networks to enhance network resource utilization and increase the quality of service.(click <a href="JavaScript:newPopup('TechnicalProgram_Tutorials_NetworkCoding.php');" class="styleWithUnderline">
here</a> for more details).
