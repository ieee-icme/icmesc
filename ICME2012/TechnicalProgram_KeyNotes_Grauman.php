<p>  <i> <strong>Focusing Human Attention on the "Right" Visual Data</strong>
</i></p>

<p>  <i> <strong>Abstract:</strong>
</i></p>
<p><i>Widespread visual sensors and unprecedented connectivity have left us awash with visual data---from online photo collections, home videos, news footage, medical images, or surveillance feeds.  Which images and videos among them warrant human attention?  This talk focuses on two problem settings in which this question is critical: supervised learning of object categories, and unsupervised video summarization.  In the first setting, the challenge is to sift through candidate training images and select those that, if labeled by a human, would be most informative to the recognition system.  In the second, the challenge is to sift through a long-running video and select only the essential parts needed to summarize it for a human viewer.  I will present our recent research addressing these problems, including novel algorithms for large-scale active learning and egocentric video synopses for wearable cameras.  Both domains demonstrate the importance of "semi-automating" certain computer vision tasks, and suggest exciting new applications for large-scale visual analysis.
</i></p>

<p><i><strong>About the Speaker:</strong>
</i></p>
<p><i>Kristen Grauman is the Clare Boothe Luce Assistant Professor in the Department of Computer Science at the University of Texas at Austin.  Her research in computer vision and machine learning focuses on visual search and object recognition.  Before joining UT-Austin in 2007, she received her Ph.D. in the EECS department at MIT, in the Computer Science and Artificial Intelligence Laboratory.  She is an Alfred P. Sloan Research Fellow and Microsoft Research New Faculty Fellow, and a recipient of NSF CAREER and ONR Young Investigator awards.  She and her collaborators were recognized with the CVPR Best Student Paper Award in 2008 for work on hashing algorithms for large-scale image retrieval, and the Marr Best Paper Prize at ICCV in 2011 for work on modeling relative visual attributes.
</i></p>
  
