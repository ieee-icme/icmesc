<p> <strong> LOOK2 - A video-based system for real-time notification of relevant traffic events </strong>
</p>


<p>Abstract: We demonstrate our novel video-based real-time trafﬁc event notiﬁcation and veriﬁcation system LOOK2. It generates fast and reliable trafﬁc information about relevant trafﬁc  state  and  road  conditions  changes  on  observed  roads. It utilizes installed road-side sensors providing low-level trafﬁc and environmental  data,  as well as video  sensors  which  gain high-level trafﬁc information from live video analysis. Spatio- temporal data fusion is applied on all available trafﬁc and environmental  data  to  gain  reliable  trafﬁc  information.  This trafﬁc information is published by a DATEXII compliant web service to a web-based  trafﬁc desk application.  Road network and  trafﬁc  channel  operators  receive  real-time  and  relevant trafﬁc event notiﬁcations by using this application. The system also enables a visual veriﬁcation  of the notiﬁed situations.
</p>

