<p> <strong> Surround Sound Using Variable-Ambisonics and Variable-Polar Pattern Theories</strong>
</p>


<p>Abstract: The technologies on display are those of Variable-Ambisonics and Variable-Polar Pattern Reproduction. These technologies are demonstrated using virtual 2-dimensional speaker layouts via binaural headphone reproduction. The technologies offer benefits over standard pair-wise panning of surround sound by offering on-the-fly changing of speaker count, source width control and per sound source rendering. The technologies also overcome problems of Ambisonics and Ambisonic based reproduction methods by allowing each sound source to be individually rendered by decoder type, of an arbitrary rather than fixed order and the mixing of various orders within the same reproduction system.
</p>

