                <td  colspan="3"><div align="left" class="STYLE2">
                  <strong>Expert Talks on Innovating the Future  Leveraging the Past - Time Machine  Session</strong>
				  <p> </p>
                  Download <a href="http://www.icme2012.org/download/ICME2012_Call-for-TimeMachine_NewFormat_final.pdf" class="styleWithUnderline" target="_blank">PDF  version of Call For Expert Talks</a>
				  <p></p>
                  Multimedia  research is moving ahead in leaps and bounds. In order to  pursue the most innovative and productive paths forward, we need an in-depth  understanding of where we have already been. The Time Machine Session at the  ICME 2012 is dedicated to the principle of improving the future by leveraging  valuable insights from the past. The session will consist of a series of expert  talks that re-introduce ideas that were published &quot;before their time&quot;  and, as a result, were never fully exploited. A &quot;Time Machine Topic&quot;  is distinguished by the fact that subsequent technological and social  developments have led to a renewal of its relevance, making it currently of  critical interest and value to the multimedia research community. A Time  Machine Talk covers not only the original idea, but also explains why it  currently deserves renewed attention and how it can influence the future of  multimedia research.
				  <p></p>
                  Time  Machine Talks should reflect expert-level understanding of the technological  and social develop-ments that have taken place in the field of multimedia and  have brought about renewed relevance of past concepts. These developments  include, but are not limited to:<br />
                    - Expansion in the volume, diversity  and sources of multimedia content<br />
                    - Increase in the size, speed and  sophistication of distribution networks <br />
                    - Improvement of computing  infrastructures in terms of processing, storage and distribution,<br />
                    - Growth of the variety and capacity  of user end devices<br />
                    - Development of user expectations  for new multimedia applications
					<p></p>
                  In  sum, the goals of the Time Machine Session are to stimulate the creative  thinking of today's multimedia researchers and to maintain the breadth of the  solution space in which we develop new algorithms and approaches. Additionally,  we believe that Time Machine Talks can help streamline and defragment the  innovation process, by encouraging reproduction and reducing redundancy.  Finally, we hope that the Time Machine Session will stimulate interesting and  productive discussion in the community.
				  <p></p>
                  <strong>Selection</strong><br />
                    From  the pool of submissions, a panel will make a selection of talks for  presentation at the Time Machine Session. The decision will be made on using  the following criteria:<br />
                    - Renewed  relevance of the idea for today's multimedia researchers and research domains  as set out in the general <a href="http://www.icme2012.org/CallForPapers_index.php" class="styleWithUnderline">ICME 2012 CFP</a><br />
                    - Scope  of the potential impact of the re-introduction of the idea on innovation in the  multimedia research community<br />
                    - Importance  of re-introduction of the idea to prevent the community from wasting time by  &quot;reinventing the wheel&quot;<br />
                    - Presentation of the idea i.e.,  compelling argumentation and engaging presentation style
					<p></p>
                  <strong>Submission Format</strong><br />
                    The  submission consists of three parts:<br />
                    1. The  reference (i.e., bibliographic citation) of the paper that originally  introduced the idea (pub-lished at least five years ago and still publicly  available),<br />
                    2. A  three minute video summarizing the idea and explaining why at the present  moment its time is finally ripe,<br />
                    3. A 300-400 word abstract to accompany  the Time Machine Talk in the ICME 2012 program.<br />
                    Note  that the person submitting the proposal does not necessarily need to be one of  the authors of the original paper.
					<p></p>
                  The  submissions email (as well as any questions on the Time Machine Session or the  submission process) should be sent to the ICME 2012 Innovation and Demo Chairs:  Martha Larson (<a class="styleWithUnderline" href="mailto:m.a.larson@tudelft.nl">m.a.larson@tudelft.nl</a>)  and Mercan Topkara (<a class="styleWithUnderline" href="mailto:mtopkara@us.ibm.com">mtopkara@us.ibm.com</a>)
				  <p></p>
                  <strong>Upon Acceptance</strong><br />
                    Speakers  for all accepted submissions will be required to sign a commitment to present  at the conference within 30 days of notification.<p></p>
                  <strong>Important Dates:</strong><br />
                    - Submission of Proposals Due: 5th  March, 2012<br />
                    - Notification of Decision: 26th  March, 2012<br />
                    - ICME 2012 in Melbourne, Australia:  9th ¨C 13th July, 2012
