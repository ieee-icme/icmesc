<h1>Travel and Visa</h1>

<h2>Visa information</h2>

<p align="justify">International attendees requiring a visa to visit Spain should contact the Spanish Embassy or Consulate nearest
them. The visa application process should be initiated well in advance of the estimated departure date.</p>
<p align="justify">If you require a Visa letter for visa application, please fill in the request form via the <a href="registration.php"><u>ICME 2011 Online Registration System</u></a>.</p>

<p align="justify">For additional information please visit the web site of the <a href="http://www.maec.es/en/Home/Paginas/HomeEn.aspx"><u>Spanish Ministry of Foreign Affairs</u></a>. The "<a href="http://www.spain.info/antes_del_viaje/consejos-practicos/index.html?l=en"><u>Practical Info</u></a>" section from <a href="http://www.spain.info/en/"><u>Turespa&ntilde;a</u></a> may be of help in finding the nearest Spanish Embassy. You can also search for the <a href="http://w3.bcn.es/barcelonainternacional/0,4022,229724149_231162515_3,00.html"><u>Consulates</u></a> in Barcelona.</p>

<h2>Transportation</h2>
<h3>By air</h3>
<p align="justify"><span class="indented"><a href="http://www.aena.es/csee/Satellite?pagename=subHome&amp;Language=EN_GB&amp;c=Page&amp;SiteName=BCN"><u>Barcelona-El Prat airport</u></a>, completely renovated in 1990/91 and expanded in 2009 with the construction of a new terminal area and the extension of the airfield with a new runway, in addition to other relevant infrastructures. It is located at 20 minutes from the conference venue and 25 minutes from the city centre. In 2009, it handled 27,421,682 passengers and 278,981 flight operations, and in 2010 has been awarded as the best airport Europe.</span></p>
<p align="justify">The Barcelona-El Prat airport is the origin/destination of direct flights to/from 150 different locations, including one or several flights a day to the main cities of Europe.</span></p>
<p align="justify">A Barcelona-Madrid air shuttle (from 6.45 to 22.45 h) also facilitates intercontinental connections.</span></p>

<h3>By road</h3>
<p align="justify"><span class="indented">The land communications are basically designed on the network of motorways, which radiate towards the most important economic areas of the rest of Spain and connect with the French motorway system to the rest of Europe.</span></p>
<p align="justify">The ring road around the city has greatly reduced the traffic passing through the city, making city traffic flow more smoothly.</span></p>

<h3>By train</h3>

<p align="justify"><span class="indented">Barcelona is also an important railway link, connected favorably with the rest of Spain and with Europe. TALGO trains connect daily Barcelona with Paris, Geneva, Bern, Zurich and Milan in comfortable night journeys. In 2008 a new high-speed train connected Barcelona with Madrid and Seville. For more details and timetables: <a href="http://www.renfe.com/EN/viajeros/index.html"><u>RENFE</u></a>.</span></p>

