<h1>Second Call For Papers</h1>
<h2>IEEE International Conference on Multimedia and Expo (ICME 2011)</h2>
<p>July 11-15, 2011 &#8226; BARCELONA, Spain</p>
<p>Download <a href="files/ICME2011_CFP1014.pdf" target="_blank"><u>PDF version</u></a> or <a href="files/ICME2011_CFP1014.txt" target="_blank"><u>TXT version</u></a> of this call for papers.</p>
<p align="justify">IEEE International Conference on Multimedia & Expo (ICME) has been the flagship multimedia conference sponsored by four IEEE societies since 2000. It serves as a forum to promote the exchange of the latest advances in multimedia technologies, systems, and applications from both the research and development perspectives of the circuits and systems, communications, computer, and signal processing communities. An Exposition of multimedia products, animations and industries will be held in conjunction with the conference.</p>

<p align="justify">Authors are invited to submit a full paper (two-column format, 6 pages) according to the guidelines available on the conference website at <a href="http://www.icme2011.org">http://www.icme2011.org</a>. Reviewing will be double blind. Only electronic submissions will be accepted. Topics of interest include, but are not limited to:
<ul>
<li>Speech, audio, image, video, text processing</li>
<li>Signal processing for media integration</li>
<li>Interactive 3D media and immersive environments</li>
<li>Multi-modal multimedia computing systems and human-machine interaction</li>
<li>Multimedia communications and networking</li>
<li>Multimedia compression</li>
<li>Multimedia security and privacy</li>
<li>Multimedia databases and digital libraries</li>
<li>Multimedia applications and services</li>
<li>Media content analysis and search</li>
<li>Hardware and software for multimedia systems</li>
<li>Multimedia standards and related issues</li>
<li>Multimedia quality assessment</li>
</ul>
</p>

<p align="justify">ICME 2011 showcases high quality oral and poster presentations and demo sessions. Best paper, poster and demo awards will be selected and recognized in the conference. Extended versions of oral papers will be considered for potential publication in a special section of IEEE Transactions on Multimedia. Accepted papers have to be registered and presented; otherwise they will not be included in the IEEE Xplore Library.</p>

<p align="justify">ICME 2011 features IEEE societies sponsored workshops, as well as call for workshop proposals. We encourage researchers, developers and practitioners to organize workshops on various new emerging topics. Industrial exhibitions are held in conjunction with the main conference. Job fairs, keynote/plenary talks and panel discussions are other conference highlights. Proposals for Tutorials and Workshops are invited. Please visit the ICME 2011 website for submission details.</p>

