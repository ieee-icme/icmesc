<h2>Paper/Demo Registration</h2>
<ol>
  <li>Accepted work must have at least one author registered at a non-student rate by <font color="#770000"><strong>April 30, 2011</strong></font>.</li>
  <li>One of the authors (or a qualified substitute) must present the paper/demo at the conference/workshops in order to avoid automatic withdrawal of the paper/demo from the conference.</li>
  <li>Each main conference non-student registration can cover <strong>TWO</strong> accepted works in ICME 2011. Each accepted work can be either:
    <ol type="a">
      <li>An accepted ICME 2011 regular paper (6 pages);</li>
      <li>An accepted ICME 2011 industrial/application short paper (4 pages);</li>
      <li>An accepted ICME 2011 demo (without associated paper); or</li>
      <li>An accepted ICME 2011 workshop paper.</li>
    </ol>
  </li>
  <li>Each workshop registration can cover up to 2 workshop papers in the ICME workshop program.</li>
</ol>

<h3>Workshop authors should know:</h3>
<ol>
  <li> Workshop registrations cover workshops and tutorials on Mon (July 11) and Fri (July 15), as well as the ICME Expo.</li>
  <li>Workshop authors who also have main conference submissions accepted, e.g. regular paper, industrial track paper or demo, can use one main conference registration to cover one main conference presentation and one workshop presentation. For example:
    <ol type="a">
      <li>1 regular paper + 1 workshop;</li>
      <li>1 demo + 1 workshop;</li>
      <li>1 industrial track paper + 1 workshop; or</li>
      <li>One workshop registration can cover up to two workshop presentations.</li>
    </ol>
  </li>
  <li>Workshop authors who do not have main conference papers can also register at main conference rate to present their workshop papers, and have the benefit of attending the main conference. Reception and banquet are included in the main conference registration subject to a small reservation fee.</li>
  <li>Reception and banquet are not included in workshop registration.</li>
</ol>
