<h2><u><a href="files/TMM_SI_Invitation_List.pdf" target="_blank">List of papers invited to submit to IEEE Trans. On Multimedia ICME 2011 Special Issue</a></u></h2>
<p align="justify">The special issue invites a selected number of top-ranking papers (Oral Paper and Top 15% Paper) published in ICME2011 to submit an extended version of the conference papers to IEEE Trans. On Multimedia (T-MM). The extended version must contain at least 30% new content compared to the conference version. The new content may include descriptions of technical and implementation details, improved algorithms, more or better experiment results, etc. The authors are required to submit a separate document describing: 1) the paper number of their corresponding ICME 2011 work, 2) the difference between the extended version and the conference version. Please note that if you have multiple papers accepted by ICME 2011, only the paper that is accepted for oral presentation or as a Top 15% paper is invited. Non-qualified paper will be administratively rejected during submission.</p>

<p align="justify">All the submitted papers will go through a rigorous review process as that for the regular T-MM paper submissions. Given the tight schedule, there will not be enough time for major revision. If it is determined that the paper cannot be accepted (A) or accepted with minor revision (AQ), the paper will be rejected. Therefore when preparing your submission, it is wise to try your best to make your paper publishable as it is.</p>

<p align="justify">Papers should be formatted according to the IEEE Transactions on Multimedia guidelines for authors (see: <a href="http://www.ieee.org/organizations/society/tmm/author_info.html" target="_blank">http://www.ieee.org/organizations/society/tmm/author_info.html</a>). Mandatory overlength page charges and color charges will apply. Manuscripts (both 1-column and 2-column versions are required) should be submitted electronically through the online IEEE manuscript submission system at http://tmmieee.manuscriptcentral.com/. When selecting a manuscript type, authors must click on ICME 2011 Special Issue. A copyright form with the manuscript number on the top of the page is required to be completed, signed and faxed to 1-732-562-8905 at the time of submission.</p>

