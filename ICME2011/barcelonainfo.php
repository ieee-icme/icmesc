<h1>Barcelona Information</h1>

<p>Hosting City: Barcelona, Spain (<a href="files/BarcelonaInformation.pdf">View Overview in PDF</a>)</p>

<p align="justify">Barcelona was founded over two thousand years ago, on the Mediterranean coast, between two rivers. It is located in
the north east of the Iberian Peninsula, just a short distance from France. Since its beginnings, the city has been the traditional gateway to Spain. Romans, Arabs and Christians passed through it, as did the diverse cultures which came to enrich its heritage. The traces of this history and diversity can be found as you walk through the city; through its Gothic Quarter built on the Roman ruins; through its art-nouveau Eixample district, which is dominated by Gaud&iacute;&#8217;s exuberant architecture, yet at the same time reveals an ordered and rational urban layout. Diversity and harmony also flourish in the character of the Barcelona people, who are enterprising and hard-working, have a hedonistic streak, and are, in particular, civic-minded and lovers of culture.</p>

