<h2>Geography</h2>

<p align="justify">Barcelona is the capital of Catalonia, an autonomous community located on the northeastern coast of Spain just south of the French border. It is bounded by the Collserola ridge and the Bes&ograve;s and Llobregat rivers. The city is less than 150 kilometers from the Pyrenees and due to its location on the shores of the Mediterranean, Barcelona enjoys a warm, welcoming climate and pleasant temperatures all year round. Rainfall is occasionally heavy, with levels of around 1,000 mm of rain per year. This helps to maintain the many green areas around the city.</p>

<h2>Language and Customs</h2>

<p align="justify">In Barcelona, as in the rest of Catalonia, there are two official languages: <a href="http://www.ethnologue.com/show_language.asp?code=cat"><u>Catalan</u></a>, the language of the region, and <a href="http://www.ethnologue.com/show_language.asp?code=spa"><u>Spanish</u></a>, the official language of Spain. Here are some of the most frequent Catalan expressions translated into Spanish, English and French, which will help you get around the city: <a href="http://barcelonaturisme.cat/wwwfiles/bcnweb/idioma/vocabulari.pdf" target="_blank"><u>vocabulary list</u></a> (pdf).</p>

<p align="justify">Following daylight saving time, the clocks are an hour ahead of Greenwich Meridian Time (GMT) in winter, and two hours ahead in summer. In Barcelona mealtimes are usually a little later than in the rest of Europe. Breakfast is between 8 am and 10 am and is similar to that of other countries on the continent. Lunch is served in restaurants between 1 pm and 3 pm and dinner from 9 pm until midnight.</p>

<h2>Business Hours</h2>

<p align="justify">Opening hours for most shops are approximately from 9 am to 1 pm and from 4:30 pm to 8 pm. Some department stores, shopping centers and individual shops&#8212;particularly in the city center&#8212;remain open at lunch time. Opening hours for banks are from 8 am to 3 pm, from Monday to Friday.<br>

<h2>Currency</h2>

<p align="justify">As of 2002 the euro is the official currency of Spain. You can change money at the banks as well as at exchange offices (<span style="font-style: italic;">canvi</span>) located in the city center and at tourist attractions. In general banks
are open from Monday to Friday from 8:30 am to 2 pm. All major credit cards are accepted. ATMs/cash machines are widely available. For
currency conversion rates, see <a href="http://www.oanda.com/"><u>Oanda</u></a>.</p>

<h2>Telephones and Mail</h2>

<p align="justify">To call any phone number in Barcelona from abroad you must first dial +34 and then the desired number. Calling any foreign number from Barcelona requires dialing 00 followed by the destination country's code and the desired number. Stamps can be bought in post offices and tobacco shops. Ordinary mail may be dropped off in yellow mail boxes, which are located throughout the city.</p>

<h2>Electricity</h2>

<p align="justify">As in most of Western Europe, in Barcelona the standard power supply is 220 V 50 Hz. Power plugs normally found are <a href="http://en.wikipedia.org/wiki/Domestic_AC_power_plugs_and_sockets#Type_F"><u>Type F</u></a> (<a href="http://en.wikipedia.org/wiki/Domestic_AC_power_plugs_and_sockets#Type_C"><u>Type C</u></a> plugs are also common).<br>

<h2>Medical Assistance</h2>

<p align="justify">You will receive immediate emergency medical help if needed. Travelers from EU countries will also receive medical treatment in Spain thanks to reciprocal European Union agreements. For other travelers, though emergency treatment will be given, it is a good idea to have some kind of medical insurance. The telephone number for emergencies is 061.</p>

<h2>Climate and Weather</h2>

<p align="justify">Barcelona&#8217;s location on the shores of the Mediterranean means it enjoys a warm, welcoming climate, with pleasant temperatures all the year round. It has five kilometers of beaches with every amenity, which are used by Barcelona&#8217;s inhabitants all the year round, and an Olympic marina which meets all requisites in order to enjoy nautical sports throughout the year.</p>

<p align="justify">Rainfall, which is occasionally heavy, averages 1.000 mm a year, making it possible to maintain the many green areas around the city.</p>

<p align="justify">Barcelona enjoys sunshine during all the seasons of the year. You can eat at open-air restaurants or enjoy a drink at any of its many pavement caf&eacute;s, always on the shores of the Mediterranean.</p>

