<h1>Presentation Guidelines</h1>

<h2>Oral Presentation Guidelines</h2>
<p align="justify">Each paper in an oral session is allocated 24 minutes. This includes time required for introduction of the speaker, as well as time for questions from the audience. Therefore authors are advised to prepare a 20-minute talk and leave some time for questions at the end.</p>

<p align="justify">Each lecture room will be equipped with a computer and a data projector. Authors need to copy their presentations to the computer at the Lecture Room at least 10 minutes in advance of the session in which their paper will be presented. Each lecture room will have a student volunteer that can help to provide technical support.</p>
 
<p align="justify">The formats that are allowed for presentations are Microsoft PowerPoint (.ppt/.pptx) and Portable Document Format (.pdf). In both cases, if the authors have a video or an audio file embedded in the presentation we recommend to use a video and audio codec compatible with Microsoft PowerPoint.</p>
<p align="justify">Authors needing any special audio or visual equipment must contact the Local Chair by Jun. 15, 2011 to indicate their request.</p>

<p>Presenters are requested to:</p>
<ul>
  <li> be present at the podium (in the correct room) at least 10 minutes before the start of their session. Oral presenters should introduce themselves to the session chair as soon as they arrive.</li>
  <li>let the session chair know of any special assistance during their talk before the session.</li>
</ul>

<h2><a name="guideposter"></a>Poster Guidelines</h2>
<p align="justify"><strong>NOTE: All the Industrial/Application accepted papers will be presented in POSTER format.</strong></p>
<p align="justify">These sessions enable presenters to demonstrate and discuss research, development and applications in progress to gain feedback and to establish contacts.</p>

<h3>Presentation Time</h3>
<p align="justify">The presenter is to be stationed by his/her poster and periodically make presentations of about 5 minutes and answer questions from audiences around the poster throughout the 2-hour poster session assigned. Presenters are encouraged to engage the audiences in technical discussion.</p>

<h3>Poster's Dimensions</h3>
<p align="justify">The poster's dimensions must be vertical DIN A0 in upright (portrait) format. Velcro adhesive will be provided at the conference to mount posters on the boards. The presenters are to report to the Session Chairs and put up their posters before the start of the session. All posters must be removed after the poster session.</p>
