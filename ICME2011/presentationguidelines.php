<h2>Demo Presentation Guidelines</h2>

<h3>Presentation Time</h3>
<p align="justify">The presenter is to be stationed by his/her demo, show the demonstration, and answer questions from audiences. Presenters are encouraged to engage the audiences in discussion. The presenters are to report to the Session Chairs and set up their demos before the start of the session. All demos must be dismantled after the demo session.</p>

<h3>Demo Board's Dimensions and Equipment</h3>
<p align="justify">Demonstrators are required to bring their own computing equipment and any additional network, display, sensor, etc. hardware needed for the demonstration. All presenters will be provided with a place for their demo, a little table (length 120 cm, width 60 cm, see image above), poster board (see Poster presentation above for adequate size), internet access and a power outlet (Barcelona format, 230 V 50Hz). Please bring your own adapters/power strip if necessary!</p>
