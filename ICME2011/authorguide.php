<h2>1. General Information</h2>
<p>The ICME 2011 paper submission and review process is  being conducted in a manner similar to previous ICME conferences:</p>
<ul>
  <li>Authors who wish to participate in the conference will create documents consisting of a complete description of their ideas and applicable research results in a maximum of 6 pages.</li>
  <li>Papers should be formatted according to the Author Guidelines described here. All papers must be written in English and submitted in PDF format.</li>
</ul>
<p align="justify">Paper submissions will be reviewed by experts selected by the conference committee  for their demonstrated knowledge of particular topics. The progress and results of the review process will be posted on this website, and authors will also be  notified of the review results by email.</p>

<h2>2. Requirements</h2>
<ul>
  <li>Papers should be no longer than 6 pages, including all text, figures, and references. The paper MUST be formatted according to the paper templates given in Microsoft Word or LaTeX.</li>
  <li>Papers must be submitted by the deadline date. There will be no exceptions.</li>
  <li>Accepted papers <strong>MUST</strong> be presented at the conference by one of the authors, or, if none of the authors are able to attend, by a qualified  surrogate. The presenter <strong>MUST</strong> register for the conference at one of the non-student rates offered, and <strong>MUST</strong> register before the deadline given for author registration. Failure to register before the deadline will result in automatic withdrawal of your paper from the conference proceedings and program.</li>
</ul>

<h2>3. Preparation of the Paper and Submission</h2>
<h3>3.1	Document Formatting</h3>
<ul>
  <li>All submissions must be in letter-sized PDF.</li>
  <li>All fonts and subsets must be embedded. Times New Roman font is strongly encouraged.</li>
  <li>Detailed examples about the paper format using LaTeX  and Microsoft Word are available in this  Guidelines.</li>
</ul>

<h3>3.2	Templates</h3>
<p>The  following style files and templates are available for users of LaTeX and Microsoft Word:</p>
<ul>
  <li>Example submission with detailed instructions: <a href="files/icme2011template.pdf">icme2011template.pdf</a></li>
  <li>Microsoft Word template: <a href="files/icme2011template.doc">icme2011template.doc</a></li>
  <li>Latex template (gzipped tar file): <a href="files/icme2011template.tar.gz">icme2011template.tar.gz</a></li>
  <li>Latex template (zip file): <a href="files/icme2011template.zip">icme2011template.zip</a></li>
  <li>Latex template (rar file): <a href="files/icme2011template.rar">icme2011template.rar</a></li>
</ul>
<p align="justify">We recommend that you use  the Word file or LaTeX files to produce your document, since they have been set up to meet the formatting guidelines listed above. When using these files, double-check the paper size in your page setup to make sure you are using the letter-size paper layout (8.5 inch X 11 inch). The LaTeX environment files specify suitable margins, page layout, text, and a bibliography style.</p>
<p align="justify">In particular, with LaTeX, there are cases where the top-margin of the resulting  Postscript or PDF file does not meet the specified parameters. In this case, you may need to add a \topmargin=0mm command just after the \begin{document} command in your .tex file. The spacing of the top margin is not critical, as the page contents will be adjusted on the proceedings. The critical dimensions are the actual width and height of the page content.</p>

<h3>3.3	Electronic Paper Submission</h3>
<p>When you have your document file ready, gather the following information before entering the submission system:</p>
<ul>
  <li>Document file in PDF format</li>
  <li>Affiliation, email address, and mailing address for each author</li>
  <li>Paper title</li>
  <li>Text file containing paper abstract text, in ASCII text format (for copying and pasting into web page form)</li>
</ul>
<p align="justify">To submit your document and author information, go to the 'Paper Submission' link on the ICME 2011 homepage and follow the instructions for using the ICME 2011 Main Conference CMT Submission System:</p>
<p><a href="http://www.icme2011.org/submission.php">http://www.icme2011.org/submission.php</a></p>
<p align="justify">The submission system will present an entry form to allow you to enter the paper title, abstract text, review category, and author contact information.</p>
<p align="justify">ALL authors must be entered in the online form, and must appear in the online form in the same order in which the authors appear on the PDF.</p>

<h2>4. Instructions and Policies</h2>
<p align="justify"><strong>Double blind review:</strong> ICME 2011 reviewing is double blind, which means that authors do not know the names of the reviewers of their papers, and reviewers do not know the names of the authors. Please read Section 2 of the <a href="files/icme2011template.pdf">example paper</a> for detailed instructions on how to preserve anonymity. Information that may identify the authors in the acknowledgments (e.g., co-workers and grant IDs) and in the supplemental material (e.g., titles in the movies, or attached  papers) should also be avoided. Also remember to avoid providing links to websites that identify the authors.</p>
<p align="justify"><strong>Dual submissions:</strong> By submitting a manuscript to ICME, the authors guarantee that it has not been  previously published (or accepted for publication) in substantially similar form. Furthermore, no paper which contains significant overlap with the contributions of this paper either has been or will be submitted during the ICME 2011 review period to either a journal or a conference.</p>
<p align="justify"><strong>Author's responsibilities:</strong> If there are papers that may appear to violate any of these conditions, then it is the authors' responsibility to (1) cite these papers (preserving anonymity as  described in Section 2 of this <a href="files/icme2011template.pdf">example paper</a>), (2) argue in the body of your paper why your ICME paper is nontrivially different from these concurrent submissions, and (3) include anonymized versions of those papers in the supplemental material.</p>
<p align="justify"><strong>Supplemental  material</strong>: Authors may optionally upload supplemental material. Typically, this material  might include: </p>
<ul>
  <li>a short presentation summarizing the paper,</li>
  <li>videos of results that cannot be included in the main paper,</li>
  <li>screen recording of the running program</li>
  <li>anonymized related submissions to other conferences and journals, and</li>
  <li>appendices or technical reports containing extended proofs  and mathematical derivations that are not essential for understanding of the paper.</li>
</ul>
<p align="justify">Note that the contents of the supplemental material should be referred to appropriately in the paper and that reviewers are not obliged to look at it.</p>
<p align="justify">All supplemental material must be zipped into a single file. There is a 30MB limit on the size of this file.</p>

