<h1>Camera-Ready Submission Guidelines</h1>
<br/>
<h2>1. General Information</h2>
<ul>
  <li>ICME 2013 requires authors to submit the title, list of authors, a brief abstract of about 100 to 150 words for each   		camera-ready paper.</li>
  <li>Make sure that the abstract, authors' names and affiliations in the submission system are identical to that shown in the camera-ready paper.</li>
  <li>Authors are required to use the new <strong><a href="#crt"><u>Camera-Ready Templates</u></a></strong>.</li>
  <li>Submission of the camera-ready paper needs to be done electronically via the ICME 2013 <strong><a href="crsubmission.php"><u>Camera-Ready Submission</u></a></strong> page.</li>
</ul>

<h2>2. Requirements</h2>
<ul>
  <li>Papers should be no longer than 6 pages, including all text, figures, and references. The paper <strong>MUST</strong> be formatted according to the <strong><a href="#crt"><u>Camera-Ready Templates</u></a></strong> given in Microsoft Word or LaTeX.</li>
  <li>A maximum of two additional pages per paper is allowed at <strong>125 USD</strong> per page, which will be added to the registration fee.</li>
  <li>Papers must be submitted by the <a href="importantdates.php"><strong><u>Deadline Date</u></strong></a>. There will be no exceptions.</li>
  <li>Accepted papers <strong>MUST</strong> be presented at the conference by one of the authors, or, if none of the authors are able to attend, by a qualified  surrogate. The presenter <strong>MUST</strong> register for the conference at one of the non-student rates offered, and <strong>MUST</strong> register before the deadline given for author registration. Failure to register before the deadline will result in automatic withdrawal of your paper from the conference proceedings and program.</li>
</ul>

<h2>3. Preparation of the Paper and Submission</h2>
<h3>3.1	Document Formatting</h3>
<ul>
  <li>All submissions must be in letter-sized PDF.</li>
  <li>All fonts and subsets must be embedded. Times New Roman font is strongly encouraged.</li>
  <li>Detailed examples about the paper format using LaTeX  and Microsoft Word are available in this Guidelines.</li>
</ul>

<h3>3.2	<a name="crt">Camera-Ready Templates</a></h3>
<p>The  following style files and templates are available for users of LaTeX and Microsoft Word:</p>
<ul>
  <li>Example submission with detailed instructions: <a href="files/template/icme2013templatecr.pdf">icme2013templatecr.pdf</a></li>
  <li>Microsoft Word template: <a href="files/template/icme2013templatecr.doc">icme2013templatecr.doc</a></li>
  <li>Latex template (gzipped tar file): <a href="files/template/icme2013templatecr.tar.gz">icme2013templatecr.tar.gz</a></li>
  <li>Latex template (zip file): <a href="files/template/icme2013templatecr.zip">icme2013templatecr.zip</a></li>
</ul>
<p align="justify">We recommend that you use  the Word file or LaTeX files to produce your document, since they have been set up to meet the formatting guidelines listed above. When using these files, double-check the paper size in your page setup to make sure you are using the letter-size paper layout (8.5 inch X 11 inch). The LaTeX environment files specify suitable margins, page layout, text, and a bibliography style.</p>
<p align="justify">In particular, with LaTeX, there are cases where the top-margin of the resulting  Postscript or PDF file does not meet the specified parameters. In this case, you may need to add a \topmargin=0mm command just after the \begin{document} command in your .tex file. The spacing of the top margin is not critical, as the page contents will be adjusted on the proceedings. The critical dimensions are the actual width and height of the page content.</p>

<h3>3.3 IEEE Xplore Compliance Using PDF eXpress</h3>
<ul>
  <li>All camera-ready submissions <strong>MUST</strong> be checked for IEEE eXplore compliance before submission to the ICME 2013   			website.</li>
  <li>To check whether your PDF file is IEEE Xplore-compatible, please use the PDF eXpress service at <a href="http://www.pdf-express.org/" ,="" target="_blank"><u><strong>http://www.pdf-express.org</strong></u></a>. Please log on using &ldquo;<font color="#770000"><strong>icme2013x</strong></font>&rdquo; as the Conference ID.</li>
  <li>A detailed user guide to the PDF eXpress can be found <a target="_blank" href="files/PDF_eXpress_icme2013x.pdf"><strong><u>here</u></strong></a>.</li>
  <li>You can also use PDF eXpress to convert your source file(s) to IEEE Xplore-compatible PDF.</li>
  <li>ICME 2013 will NOT accept camera-ready papers that are not IEEE Xplore-compatible.</li>
</ul>

<h3>3.4	Electronic Paper Submission</h3>
<ul>
  <li>When you have your document file ready, gather the following information before entering the submission system:</li>
  <ul>
    <li>Document file in PDF format</li>
    <li>Affiliation, email address, and mailing address for each author</li>
    <li>Paper title</li>
    <li>Text file containing paper abstract text, in ASCII text format (for copying and pasting into web page form)</li>
  </ul>
  <li>The filename <strong>MUST</strong> be in the form of &ldquo;<font color="#770000"><strong>paper_id.pdf</strong></font>&rdquo;.</span> For ICME 2013 workshop paper, please add &lsquo;W&rsquo; before paper ID. That is, workshop paper with paper ID 100 should be written as W100.</li>
  <li>To submit your document and author information, go to the <strong><a href="crsubmission.php"><u>Camera-Ready Submission</u></a></strong> page and follow the instructions.</li>
  <li>Only PDFs that are verified or generated by IEEE PDF eXpress can be accepted as camera-ready manuscripts.</span></li>
  <li>All authors must be entered in the online form, and must appear in the online form in the same order in which the authors appear on the PDF.</li>
</ul>

<h3>3.5 Copyright</h3>
<ul>
  <li>Electronic copyright forms are to be submitted to IEEE from the <a href="copyright.php"><u><strong>Copyright Submission</strong></u></a> page.</li>
  <li>Failure to do so will render the paper inadmissible for publication in  IEEE Xplore, and the paper will subsequently be removed from the  proceedings.</li>
</ul>

