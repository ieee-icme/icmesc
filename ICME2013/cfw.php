<h1>Workshops</h1>
<h2>Call for Workshop Proposals</h2>
<p>July 15-19, 2013 &#8226; Fairmont Hotel, San Jose, California, USA</p>
<p>Download <a href="files/calls/ICME2013_CFW201212.pdf" target="_blank"><u>PDF version</u></a> of this call for workshop proposals.</p>
<p align="justify">Workshop proposals are solicited for ICME 2013. The workshops should be clearly focused and be facilitating active discussions and constructive interactions on current or emerging topics of interest. They should allow members of the community to share and discuss approaches, methods, innovative technologies for multimedia, and new concepts on research topics pertinent to the main conference. These topics would bring to the interest of the multimedia research community or bridge the community with experts from other areas.</p>

<p align="justify">Workshop proposals for ICME 2013 should include:
<ul>
<li>The title, goals, and duration of the workshop (half-day or full-day);</li>
<li>The specific topics  that will be addressed by the workshop;</li>
<li>The relevance and significance of this workshop to the main conference;</li>
<li>The history of the workshop if it is not the first version;</li>
<li>A draft call for papers for the proposed workshop with:
  <ol type="a">
    <li>The names, affiliations, email addresses, and short bios of the workshop organizers, who should be experts in the related topics and preferably from multiple institutions;</li>
    <li>The paper selection  process;</li>
    <li>A list of potential  Program Committee members and their affiliations.</li>
  </ol>
</li>
</ul>
</p>

<p align="justify"><em>Workshop proposals should not exceed four pages</em> and should be submitted to the Workshop Chairs, Zhengguo Li (ezgli [at] i2r.a-star.edu.sg), Maria Luisa Sapino, (mlsapino[at] di.unito.it), and Timothy K. Shih, (timothykshih[at]gmail.com). </p>

<p align="justify">Workshop proposals will be evaluated based on various criteria including the theme of the workshop, its relevance to the main conference, and the overall balance of the conference workshop program, as well as the quality and level of details of the proposal.</p>

