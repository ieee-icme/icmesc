<h2>Cancellation/Refund Policy</h2>
<h3 style="margin-left:0">General Attendees</h3>
<p align="justify">Cancellations/refund request should be made in writing to icmereg@ieee.org. Cancellations will be subject to a US$105  cancellation fee before <strong>June 1, 2013. NO REFUNDS</strong> will be made after <strong>JUNE 1, 2013.</strong></p>
<h3 style="margin-left:0">Author Refund</h3>
<p align="justify">At least one author of the paper must be  registered at a non-student and non-life-member rate in order for paper to be included in the  proceedings. If complete payment is not received by the author registration deadline, the paper will be pulled from the publications in ICME 2013. Author registration is <strong>non-refundable.</strong></p>
<h3 style="margin-left:0"><u>Author No Shows</u></h3>
<p align="justify">ICME 2013 requires that each accepted  paper be presented by one of the authors in-person at the conference site  according to the schedule published. Any paper accepted into the technical program, but not presented on-site will be withdrawn from the proceedings published on IEEE Xplore.</p>
<p align="justify">Registration fees can be transferred to another co-author under special circumstances. All transfer requests must be made in writing from the proceedings published in IEEE Xplore.</p>
<p align="justify">IEEE reserves the right to exclude a paper from distribution after the conference (e.g. removal from IEEE Xplore) if the paper is not presented at the conference.</p>

