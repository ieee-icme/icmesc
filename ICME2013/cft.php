<h1>Tutorials</h1>
<h2>Call for Tutorials</h2>
<p>July 15-19, 2013 &#8226; Fairmont Hotel, San Jose, California, USA</p>
<p>Download <a href="files/calls/ICME2013_CFT201209.pdf" target="_blank"><u>PDF version</u></a> of this call for tutorial proposals.</p>
<h3>Tutorials are distinguished lectures on hot topics in multimedia, and they are FREE to all conference registrants.</h3>
<h3>Honorarium will be provided to speakers.</h3>
<p align="justify">The IEEE International Conference on Multimedia &amp; Expo (ICME) has been the flagship multimedia conference sponsored by four IEEE societies since 2000. It serves as a forum to promote the exchange of the latest advances in multimedia technologies, systems, and applications from both the research and development perspectives of the circuits and systems, communications, computer and signal processing communities. ICME 2013 will be held in San Jose, California from July 15-19, 2013.</p>

<p align="justify">The ICME 2013 Organizing Committee invites proposals for tutorials to be held on the first day of the conference <strong><u>Monday July 15, 2013</u></strong>. We seek <strong><u>half-day</u></strong> tutorials on established or emerging research topics within the field of multimedia and related topics, such as:</p>
<ul>
  <li>Multimedia computing systems and human-machine interaction</li>
  <li>Interactive media and immersive environments</li>
  <li>3D visualization, animation, gaming, and virtual reality</li>
  <li>3D imaging and 3DTV</li>
  <li>Speech, audio, image, video, and text processing</li>
  <li>Signal processing for media integration</li>
  <li>Multimedia content analysis and search</li>
  <li>Multimedia databases and digital libraries</li>
  <li>Multimedia signal processing</li>
  <li>Multimedia security and privacy</li>
  <li>Multimedia analysis and social media</li>
  <li>Multimedia communications and networking</li>
  <li>Multimedia compression and standards</li>
  <li>Multimedia applications and technologies</li>
  <li>Hardware and software for multimedia systems</li>
  <li>Multimedia quality assessment</li>
</ul>

<p align="justify">Proposals prepared in a single-column PDF file, should include the following information:</p>
<ol>
  <li>Title of the tutorial</li>
  <li>Area of the tutorial (refer to the above list of topics)</li>
  <li>300 word abstract (for inclusion in registration materials)</li>
  <li>Keywords</li>
  <li>Target audience and prerequisite knowledge of audience</li>
  <li>Up to 4 pages full description of the tutorial, including learning objectives, table of contents, and teaching modules in details (review, in-depth content, etc...)</li>
  <li>Instructor(s) and their credentials (4-page CV for each instructor, including selected relevant publications in the proposed area of the tutorial)</li>
</ol>

