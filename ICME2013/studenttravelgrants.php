<h1>Student Travel Grants</h1>
<p>Download <a href="files/calls/ICME2013_CallForTravelGrants.pdf" target="_blank"><u>PDF version</u></a> of this call for student travel grant applications.</p>
<p align="justify">Thanks to the generous support from our sponsors, ICME 2013 is pleased to provide a number of travel grants for student participants. Each student travel grant consists of an amount to partly cover the costs of a student's travel, lodging, meals expenses and conference registration fees. The amount will be in proportion to the relative distance and travel costs of the student.</p>

<h2>Eligibility Criteria</h2>
<ol>
<li>The applicant must be a student at the time of conference registration.</li>
<li>The applicant must be the author or a co‐author of paper accepted in ICME 2013.</li>
<li>The applicant must have registered for the conference at the time of application and must attend the conference to present the paper.</li>
<li>The selected applicant must be able to provide receipts and a certification as proof of travel related expenses that have not been funded from other sources.</li>
</ol>
</p>

<h2>Application Procedure</h2>
<p align="justify">Interested students should send an explicit request for the student travel grant. A complete application should contain the following items (preferably as a single PDF document):
<ol>
<li>A <em>cover letter</em> that briefly summarizes (i) the applicant's research interests and track‐record relevant to ICME 2013 topics, and (ii) a description of how ICME 2013 will benefit his/her professional development.</li>
<li>A <em>support letter</em> from the student's advisor/supervisor that (i) confirms the applicant's student status, (ii) explains why the applicant is in need of a travel grant, and (iii) how attending ICME 2013 will benefit the student's research progress.</li>
<li>Evidence of registration for ICME 2013.</li>
</ol>
</p>

<p align="justify">Applications should be emailed by Friday June 7, 2013 to <a href="mailto:yfang@scu.edu">yfang@scu.edu</a> using the following subject line:<br />
<strong>ICME 2013 Student Travel Grant – YourName – YourPaperID(s)</strong></p>

<h2>Selection Criteria</h2>

<p align="justify">Grants will be awarded to applicants who have a genuine need for support and a strong track-record on ICME 2013 related research; while quality of the accepted paper(s) and geographic distribution will also be taken into consideration. A subcommittee consisting of organizing committee members of ICME 2013 will review each application and make the final recommendation. ICME 2013 reserves the right to make the final decision regarding all applications.</p>
<p align="justify">Applicants will be notified about the final decisions by Monday June 14, 2013.</p>

<h2>Important Dates</h2>
<span class="width400">Application Deadline:</span>Friday June 7, 2012<br/>
<span class="width400">Notification:</span>Friday June 14, 2012

