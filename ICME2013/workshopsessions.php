<br><br><b>Short Bio</b>
<p align="justify">Noboru Babaguchi received the B.E., M.E. and Ph.D. 
degrees in communication engineering from Osaka University, in 1979, 
1981 and 1984, respectively. He is currently a Professor of the 
Department of Information and Communications Technology, Osaka 
University. &gt;From 1996 to 1997, he was a Visiting Scholar at the 
University of California, San Diego. His research interests include 
image/video analysis, multimedia computing and intelligent systems. He 
has published over 200 journal and conference papers and several 
textbooks. Dr. Babaguchi received Best Paper Award of 2006 Pacific-Rim 
Conference on Multimedia (PCM2006), and Fifth International Conference 
on Information Assurance and Security (IAS2009). He is on the editorial 
board of Multimedia Tools and Applications, Advances in Multimedia, and 
New Generation Computing. He served as a workshop Co-chair of 3rd 
International Workshop on Multimedia Information Retrieval (MIR2001), a 
Track Co-chair of 2006 IEEE International Conference on Multimedia &amp;
 Expo (ICME2006), a General Co-chair of the 14th International 
MultiMedia Modeling Conference (MMM2008), a Sub-Track Chair (Image, 
video, and multimedia signal processing) of 2009 APSIPA Annual Summit 
and Conference (APSIPA ASC 2009), a General Co-Chair of ACM Multimedia 
2012, a Track Co-Chair of 21st International Conference on Pattern 
Recognition, and an Area Chair of ICME2013. He also served on program 
committee of international conferences in multimedia related fields. He 
is a Fellow of IEICE, a Senior Member of IEEE, and a member of ACM, 
IPSJ, ITE and JSAI.</p>
