<h1>&quot;Multimedia for Humanity&quot; Theme Track</h1>
<h2>Call For Papers</h2>
<h2>IEEE International Conference on Multimedia and Expo (ICME 2013)</h2>
<p>July 15-19, 2013 &#8226; Fairmont Hotel, San Jose, California, USA</p>
<p>Download <a href="files/calls/ICME2013_CFP201212-Theme.pdf" target="_blank"><u>PDF version</u></a> of this call for theme-track papers.</p>

<p align="justify">The &ldquo;Multimedia for Humanity&rdquo; Theme Track is a <strong>new technical program</strong> introduced in  ICME 2013. The goal is to encourage the deployment of multimedia methodologies, techniques and systems to benefit society and humanity. Multimedia research &amp; development has a close link with elements necessary for daily activities, which include Green Energy, Security, Transportation, Mobile Communication, Heritage &amp; Culture, Healthcare, Entertainment, Education, Social Networking, Art &amp; Design, and so on.</p>

<p align="justify">The IEEE International Conference on Multimedia &amp; Expo (ICME) has been the flagship multimedia conference sponsored by four IEEE societies since 2000. Authors are invited to submit a paper (IEEE two-column format, 4 to 6 pages) according to the guidelines available on the conference website at <a href="http://www.icme2013.org/">www.icme2013.org</a>. Reviews will be double-blind. Only electronic and anonymous submissions will be accepted. Theme sessions of interest include, but are not limited to:</p>
<ul>
<li>Healthcare, clinical, medical and rehabilitation related</li>
<li>Environment, crisis and green technologies</li>
<li>Serious gaming education and training</li>
<li>Social media and social network</li>
<li>Culture, heritage, and art</li>
<li>Other humanitarian impacts</li>
</ul>

<p align="justify">ICME 2013 aims to have high quality oral, poster and demo presentations. Evaluation of submissions to this Theme Track will focus on &ldquo;impact on humanity&rdquo; and &ldquo;implementation/performance efficiency.&rdquo; Theme Paper Awards will be announced during the conference. Accepted papers should be presented, or else they will not be included in the IEEE Xplore Library.</p>

