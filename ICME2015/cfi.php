<h1> Call For Industry Track Papers</h1>
<h2>IEEE International Conference on Multimedia and Expo (ICME 2015)</h2>
<p>June 29 - July 3, 2015 &#8226; TURIN, Italy</p>

<p align="justify">The IEEE International Conference on Multimedia & Expo (ICME) has been the flagship multimedia conference sponsored by four IEEE societies since 2000. </p>

<p> ICME 2015 will feature an industry track aiming to promote applied research and applications, and facilitate collaborations between industrial and academic members of the multimedia community. The industry track will include industry talks, demos and/or panel discussions, as well as technical papers detailing industrial research activities relevant to the scope of ICME, including but not limited to, emerging applications or standards, industry trends, technical breakthroughs, user needs, and social aspects. This track complements the EU project papers track, in that it will cover industrial research activities that are not necessarily in the scope of EU research projects.</p>


<p align="justify">In addition to specific events featuring invited papers, the industry track will also accept open submissions. Technical papers should not be longer than 4 pages and will be reviewed following the same procedure as workshop papers, where novelty, presentation quality and experimental validation will be considered. </p>

<p align="justify">Industry track papers shall be submitted through the <a href="https://cmt.research.microsoft.com/ICMEW2015"><u>ICME workshop submission track.</u></a> </p>

<p align="justify">Accepted papers must be registered and presented at the conference; otherwise they will not be included in the USB Proceedings or in the IEEE Xplore Library. </p>


