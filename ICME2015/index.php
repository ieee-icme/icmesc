<h1>IEEE International Conference on Multimedia and Expo</h1>
<p align="justify">The 2015 IEEE International Conference on Multimedia and Expo (ICME 2015) will be held in Turin, Italy from June 29 to July 3, 2015.</p> 
<p align="justify">From 2015 on, the ICME technical program will exhibit the following new features:
<ul style="margin-top:0;padding-left:1%">
      <li>In addition to regular papers (5 pages, the last page may only be used for references), the ICME technical program will also feature long papers (8 pages). The overall acceptance rate (long + short papers) will not exceed 30%.</li>
<li>High-quality long papers that have not been selected may be shortened and accepted as short papers without being re-reviewed, subject to approval from the TPC chairs.</li>
<li>ICME will complement the oral program with poster sessions dedicated to hot topics in multimedia, particularly those that have been subject of a special issue in the most important journals in the area of multimedia, such as IEEE Transactions on Multimedia, IEEE Transactions on Circuits and Systems for Video Technology, and others. Authors of accepted special issue journal papers will be invited to present their work at ICME.</li>
<li>Authors of regular journal papers (not necessarily from a special issue) may also apply for poster presentation. They will need to provide title, authors, and abstract, and certify that the paper has been accepted (with A or AQ status) for publication within the last year, and that it has not been previously presented.</li>
<li>Accepted papers should be presented, or else they will not be included in the IEEE Xplore Library.</li></ul></p>

