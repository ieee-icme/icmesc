<h1>IEEE International Conference on Multimedia and Expo</h1>
<p align="justify">The 2015 IEEE International Conference on Multimedia and Expo (ICME 2015) will be held in Torino, Italy from June 29 to July 3, 2015.</p> 

<p align="justify">The IEEE International Conference on Multimedia & Expo (ICME) has been the flagship multimedia conference sponsored by four IEEE societies since 2000. It serves as a forum to promote the exchange of the latest advances in multimedia technologies, systems, and applications from both the research and development perspectives of the circuits and systems, communications, computer, and signal processing communities. ICME also features an Exposition of multimedia products and prototypes.</p>

