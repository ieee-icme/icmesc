<p align="justify">This Call for Proposal is distributed on behalf of the Steering Committee for the 2013 and 2014 IEEE International Conference on Multimedia and Expo (ICME) to be held in summer of both years. IEEE International Conference on Multimedia &amp; Expo (ICME) has been the flagship multimedia conference sponsored by four IEEE societies since 2000. It serves as a forum to promote the exchange of the latest advances in multimedia technologies, systems, and applications from both the research and development perspectives of the circuits and systems, communications, computer, and signal processing communities. An Exposition of multimedia products, animations and industries will be held in conjunction with the conference. ICME 2013 will be the fourteenth meeting in this series and ICME 2014 will be the fifteenth in the series.</p>
<p align="justify">The conference organizing team is advised to incorporate into their proposal the following items.</p>
<ul>
  <li>Proposed Dates (approximately mid-year of 2013 or 2014)</li>
  <li>Organizing Committee Members</li>
  <ul>
    <li>Name</li>
    <li>Biographical information for General Chair(s), Technical Program Chairs, Treasurer/Finance Chair, and Publicity Chairs</li>
  </ul>
  <li>Technical Committee Members</li>
  <ul>
    <li>Name</li>
    <li>Membership in the Signal Processing Society: Multimedia Signal Processing Technical Committee, Circuits and Systems Society: Multimedia Systems and Applications Technical Committee, Communications Society: Multimedia Communications Technical Committee, and/or Computer Society: Technical Committee on Multimedia Computing</li>
  </ul>
  <li>Proposed budget. (For advice on building an IEEE budget please contact Linda Skeahan at l.skeahan@ieee.org.)</li>
  <li>Support that can be anticipated from the local government, universities and or corporations</li>
  <li>Why this location?</li>
  <ul>
    <li>Airport information</li>
    <li>Customs and Visa regulations</li>
    <li>Hotel and convention center information (i.e. space diagrams, maps, etc.)</li>
    <li>Tourist destinations (i.e. museums, natural wonders, etc.)</li>
    <li>Average weather conditions for the time of year</li>
  </ul>
</ul>

<h3>Submission of Proposal</h3>
<p align="justify">Before submitting a proposal please read the <strong>ICME Charter and Guidelines</strong> on the <a href="http://www.ieee-icme.org/index.php" target="_blank">ICME website</a>. Also, please notify the Steering Committee of your intent to propose via an e-mail (to Prof. Wenjun Zeng at zengw@missouri.edu) by 1 April, 2011.</p>

<p align="justify">Proposals for ICME are currently being accepted for the 2013 and 2014 ICME. Proposals should be sent no later than June 1, 2011. Send the proposal to Lisa Schwarzbek, Conference Services Manager, IEEE Signal Processing Society (l.schwarzbek@ieee.org).</p>

<h3>Proposal Presentation</h3>

