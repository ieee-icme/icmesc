<h1>Second Call For Papers</h1>
<h2>IEEE International Conference on Multimedia and Expo (ICME 2015)</h2>
<p>June 29 - July 3, 2015 &#8226; TURIN, Italy</p>
<p>Download <a href="files/ICME2015_CFP1014.pdf" target="_blank"><u>PDF version</u></a>  of this call for papers.</p>
<p align="justify">The IEEE International Conference on Multimedia & Expo (ICME) has been the flagship multimedia conference sponsored by four IEEE societies since 2000. It serves as a forum to promote the exchange of the latest advances in multimedia technologies, systems, and applications from both the research and development perspectives of the circuits and systems, communications, computer, and signal processing communities. ICME also features an Exposition of multimedia products and prototypes.</p>

<p align="justify">Technical program: From 2015 on, in addition to regular papers (5 pages), the ICME technical program will also feature long papers (8 pages). The overall acceptance rate (long + short papers) will not exceed 30%. Long papers aim at recognizing work of outstanding quality and will constitute the oral program. High-quality long papers that are not selected may be shortened and accepted as short papers without being re-reviewed, subject to approval from the TPC chairs.</p>


<p align="justify">Authors are invited to submit full long and regular papers (two column format, 8 and 5 pages respectively) according to the guidelines available on the conference website at http://icme2015.ieee-icme.org/. Topics of interest include, but are not limited to:</p>

<ul>
<li>Multimedia content analysis</li><li>Multimedia activity and event understanding</li><li>Multimedia search and retrieval</li><li>Mobile, location-based and other context-based multimedia</li><li>Social, user-generated, and cloud-based multimedia</li><li>3D immersion and virtual reality</li><li>Multimedia security and forensics</li><li>Human computer interaction based on multimedia</li><li>Multimedia networking and communication</li><li>Multimedia coding and compression</li><li>Multimedia signal processing and enhancement</li><li>Multimedia systems, applications, services and implementations</li>
</ul>
</p>

<p align="justify">ICME 2015 aims to have high quality oral and poster presentations. Several awards sponsored by industry and institutions will be granted. The conference will provide student author travel grants. </p>

<p align="justify">A number of workshops will be organized by the sponsoring societies. To further foster new emerging topics, ICME 2015 also welcomes researchers, developers and practitioners to organize regular workshops. Industrial exhibitions are held in parallel with the main conference. Proposals for special sessions, tutorials, panels, and demos are also invited. Please visit the ICME 2015 website for submission details</p>

