<h2><a name="pangqwe"></a>Panel I - COMPUTATIONAL IMAGING: EMPOWERING PHOTOGRAPHY FOR THE NEXT GENERATION</h2>
<h3 style="margin-left:0px">Date/Time: Wednesday, 1st July, 3:40pm-5:00pm</h3>
<h3 style="margin-left:0px">Room: Cavour</h3>
<p align="justify">Digital pictures are an increasingly large part of our lives. It is estimated that last year alone, some one trillion (1000,000,000) photos were shared on social media, the vast majority of which were captured using cameras on mobile devices. Photography has clearly been democratized to the masses, and this has profound economic and technological implications. The availability of powerful processors on mobile devices (and cloud processing) present opportunities for the deployment of novel and unprecedented imaging applications. The panel will discuss and debate this interesting landscape.</p>

