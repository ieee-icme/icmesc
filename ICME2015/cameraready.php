</p>

<h3>3.3 IEEE Xplore Compliance Using PDF eXpress</h3>
<ul>
  <li>All camera-ready submissions <strong>MUST</strong> be checked for IEEE eXplore compliance before submission to the ICME 2015 website.</li>
  <li>To check whether your PDF file is IEEE Xplore-compatible, please use the PDF eXpress service at <a href="http://www.pdf-express.org/" ,="" target="_blank"><u><strong>http://www.pdf-express.org</strong></u></a>. Please log on using the following Conference IDs: &ldquo;<font color="#770000"><strong>34918XP (for conference paper submission), 35294XP (for workshops paper submissions)</strong></font>&rdquo; .</li>
  <li>You can also use PDF eXpress to convert your source file(s) to IEEE Xplore-compatible PDF.</li>
  <li>ICME 2015 will NOT accept camera-ready papers that are not IEEE Xplore-compatible.</li>
</ul>

<h3>3.4	Electronic Paper Submission</h3>
<ul>
  <li>When you have your document file ready, gather the following information before entering the submission system:</li>
  <ul>
    <li>Document file in PDF format</li>
    <li>Affiliation, email address, and mailing address for each author</li>
    <li>Paper title</li>
    <li>Text file containing paper abstract text, in ASCII text format (for copying and pasting into web page form)</li>
  </ul>
  <li>The filename <strong>MUST</strong> be in the form of &ldquo;<font color="#770000"><strong>paper_id.pdf</strong></font>&rdquo;.</span> For ICME 2015 workshop paper, please add &lsquo;W&rsquo; before paper ID. That is, workshop paper with paper ID 100 should be written as W100.</li>
  <li>All authors must be entered in the online form, and must appear in the online form in the same order in which the authors appear on the PDF.</li>
  <li>Only<span lang="EN-US" xml:lang="EN-US"> PDFs that are verified or generated by IEEE PDF eXpress can be accepted as camera-ready manuscripts and updated to <a href="https://cmt.research.microsoft.com/ICME2015" ,="" target="_blank"><u><strong>https://cmt.research.microsoft.com/ICME2015</strong></u></a> for REGULAR PAPERS or <a href="https://cmt.research.microsoft.com/ICMEW2015/" ,="" target="_blank"><u><strong>https://cmt.research.microsoft.com/ICMEW2015/</strong></u></a> for WORKSHOP PAPERS.</span></li>
 </ul>

<h3>3.5 Copyright</h3>
<ul>
  <li>Electronic copyright forms are to be submitted to IEEE, by clicking on the “Submit copyright” link in CMT. A pdf copy of the copyright form needs to be uploaded back to CMT.</li>
  <li>Failure to do so will render the paper inadmissible for publication in  IEEE Xplore, and the paper will subsequently be removed from the  proceedings.</li>
</ul>

