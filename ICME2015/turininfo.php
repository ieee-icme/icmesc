<h1>Torino Information</h1>

<p>Hosting City: Torino, Italy</p>

<p align="justify">The city is situated on the Po river, surrounded by the majestic scenery of the Alps, Torino is a city where past and present, history and modernity coexist. Monuments, historical cafés, castles and old mansions testify its glorious past as Italy’s first modern capital. Liveliness, innovation, cultural excitement all this describes its role as a dynamic European hub for technology, science, cultural development and art. In this city of international events a business trip can turn into a stimulating and exciting experience: baroque architecture, stylish downtown shopping, galleries and museums all this makes Torino competing with big European capitals, music and shows make night life sparkling. Wines and fare make Torino experience richer and enjoyable: a walk through the “Roman quadrilateral” or a tour up the hills are the ideal option to find out old and new flavors. </p>


<h2>Language and Customs</h2>

<p align="justify">The official language is Italian. English is commonly spoken.</p>

<p align="justify">Following daylight saving time, the clocks are an hour ahead of Greenwich Meridian Time (GMT) in winter, and two hours ahead in summer.</p>

<h2>Business Hours</h2>

<p align="justify">Opening hours for most shops are approximately from 8.30 am to 12.30 pm and from 2:30 pm to 7.30 pm. Some department stores, shopping centers and individual shops&#8212;particularly in the city center&#8212;remain open at lunch time. Opening hours for banks are from 8.30 am to 2 pm, from Monday to Friday.<br>

<h2>Currency</h2>

<p align="justify">As of 2002 the euro is the official currency of Italy. You can change money at the banks as well as at exchange offices (<span style="font-style: italic;">cambio</span>) located in the city center and at tourist attractions. In general banks
are open from Monday to Friday from 8:30 am to 2 pm. All major credit cards are accepted. ATMs/cash machines are widely available.</p>

<h2>Telephones and Mail</h2>

<p align="justify">To call any phone number in Torino from abroad you must first dial +39 and then the desired number. Calling any foreign number from Torino requires dialing 00 followed by the destination country's code and the desired number. Stamps can be bought in post offices and tobacco shops. Ordinary mail may be dropped off in red mail boxes, which are located throughout the city.</p>

<h2>Electricity</h2>

<p align="justify">As in most of Western Europe, in Torino the standard power supply is 220 V 50 Hz. Power plugs normally found are <a href="http://en.wikipedia.org/wiki/Domestic_AC_power_plugs_and_sockets#Type_F"><u>Type F</u></a> (<a href="http://en.wikipedia.org/wiki/Domestic_AC_power_plugs_and_sockets#Type_C"><u>Type C</u></a> plugs are also common).<br>

<h2>Medical Assistance</h2>

<p align="justify">You will receive immediate emergency medical help if needed. Travelers from EU countries will also receive medical treatment in Italy thanks to reciprocal European Union agreements. For other travelers, though emergency treatment will be given, it is a good idea to have some kind of medical insurance. The telephone number for emergencies is 113.</p>

