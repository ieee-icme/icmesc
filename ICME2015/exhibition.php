<h2>Exhibition</h2>
<p align="justify">IEEE International Conference on Multimedia & Expo (ICME) has been the flagship multimedia conference sponsored by four IEEE societies (Communications, Computer, Circuits and Systems; and Signal Processing) since 2000. It serves as a forum to promote the exchange of the latest advances in multimedia technologies, systems and applications from both the research and development perspectives of the circuits and systems, communications, computer and signal processing communities, among others. An Exposition of multimedia products, animations and industries will be held in conjunction with the conference.</p>

<p align="justify">The goal of the Exhibit and Industrial Program is to promote applied research and applications, as well as facilitate collaborations between industrial and academia members of the multimedia community.</p>
