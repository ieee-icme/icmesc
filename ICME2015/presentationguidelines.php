<h1>Presentation Guidelines</h1>

<h2>Oral Presentation Guidelines</h2>
<p align="justify">Each full paper in an oral session is allocated <strong> 18 minutes</strong>. 
This includes time for questions from the audience, session-chair introductions and any set up that is not completed in 
advance. To achieve appropriate timing, organize your slides around the points you intend to 
make, using no more than one slide per minute. Be prepared to begin your presentation as 
soon as the prior presenter has finished; it is important to keep on schedule.</p>

<p align="justify">Your presentation can be uploaded on the conference center facilities 
  (both PDF and PPT file formats are allowed with preference for PDF to avoid font issues). 
  For the main conference oral presentations the upload must be done through the Slide Center 
  that will be opened for all the duration of the conference. Workshop oral presentation can be uploaded directly on the PC (MS Windows OS) 
  available in each lecture room.  As an alternative you can use your own laptop with the VGA connection available in each lecture 
  room (bring with you proper adaptor if your laptop does not have VGA plug). It is recommended to test the 
  presentation materials, e.g. video and audio, as well as your laptop in advance of the session to ensure compatibility. 
  It is a good idea to have your presentation material on a memory stick as a backup even if you plan to use your own laptop. 
  Each room will have personnel that can help to provide technical support
</p>
 
<p align="justify">Presenters are requested to be present in the room at least 10 minutes before the start of their session and introduce themselves to the session chair.</p>


