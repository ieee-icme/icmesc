<p align="justify">In each poster session, posters are assigned unique poster ID, which should match the poster locations. Please put up your poster during the break before your session starts, and take it 
down immediately after your session ends. A presenter must be present at your poster for all 
the session duration. If possible, more than one author should attend the session to aid in 
presentations and discussions, and to provide the presenters with the chance to rest or briefly 
view other posters. Prepare a short presentation of about 5 or 10 minutes that you can 
periodically give to those assembled around your poster throughout the poster session. If you 
need extra presentation materials, such as a video display or computer, you are required to 
bring them yourself; note that any equipment used in the poster area should be battery-
operated, since power will not be provided on the floor.</p>


<h2>Demo Presentation Guidelines</h2>

<p align="justify">The presenter is to be stationed by his/her demo, show the demonstration, and answer
questions from audiences. Presenters are encouraged to engage the audiences in discussion. 
The presenters are to report to the Session Chairs and set up their demos before the start of 
the session. All demos must be dismantled after the demo session.</p>

<p align="justify">Demonstrators are required to bring their own computing equipment and any additional 
network, display, sensor, etc. hardware needed for the demonstration. All presenters will be 
provided with a place for their demos, a table (120 cm x 60 cm), poster board (see Poster 
presentation above for size), Internet access (conference shared wi-fi only) and a power 
outlet (230 V 50Hz, see Italian power plug details <a href="http://www.power-plugs-sockets.com/italy/">here</a>).</p>


