<h1>Photo Gallery</h1>
<p align="justify">In order to benchmark the success of ICME 2011 R&D exchange and networking events held at Barcelona, Spain, you are invited to email your memorable photos to <a href="mailto:ieee.icme2011@gmail.com"><strong>ieee.icme2011@gmail.com</strong></a>. Please restrict your photo to a maximum resolution of 512x512, and specify the date and location of each photo.</p>

