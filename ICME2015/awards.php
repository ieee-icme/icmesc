<h1>Student Travel Grants</h1>

<p align="justify">Thanks to the generous supports from Qualcomm, La Salle, and IEEE NCS, ICME 2011 is pleased to provide a number of travel grants for ICME 2011 student participants. Each student travel grant consists of <strong>$500</strong> to help defray, but not necessarily to cover entirely, the costs of student's travel, lodging, meals expenses and conference registration fees.</p>
<p align="justify">Download <a href="files/ICME2011_student-travel-grant_v2.pdf"><u>PDF version</u></a> of this Student Travel Grants Announcement.</p>

<h2>Eligibility for Student Travel Grants</h2>
<ol>
  <li>The applicant has  to be a student (or has been at the time of conference registration).</li>
  <li>The applicant must be the author or co-author of an accepted paper in the conference.</li>
  <li>The applicant must register for the conference at the time of application, and attend the conference to present the paper.</li>
  <li>The grants can only be used to cover the travel expenses for applicants to attend ICME2011.</li>
  <li>If selected, the student should provide a signature to confirm that the grant he/she is receiving is not covering expenses already funded by other means.</li>
  <li>If selected, the student should provide receipts of travel expenses (e.g., travel invoices, hotel, boarding passes, etc.) that  can certify his/her attendance.</li>
</ol>
<h2>Application Procedure</h2>

<p align="justify">Interested student should send an explicit request for the student travel grant. A complete application should contain the following items (combined into one file):</p>
<ol>
  <li>A resume or CV of the student applicant.</li>
  <li>A letter that briefly summarizes (i) the applicant's research interests and accomplishments, and the relevance to ICME topics, (ii) a description of how ICME will benefit his/her professional development.</li>
  <li>3.	A short support letter from the student's advisor that (i) confirms the applicant's student status, (ii) explains why the applicant is in need of a travel grant, and (iii) how attending ICME 2011 will benefit the student's research progress.</li>
  <li>Evidence of registration for ICME 2011. </li>
</ol>
<p>Applications should be submitted by Friday  June 10, 2011 via email to <br />
<span class="indented">smao2011@gmail.com</span><br />
Please use the following format in the subject  line<br />
<span class="indented">ICME 2011 Student Travel Grant – YourPaperID – YourName</span></p>
<p align="justify">Please combine all the components into a single file. ASCII or PDF formats are strongly recommended.</p>

<h2>Evaluation and Notification</h2>
<p align="justify">Only a limited number of grants will be awarded on a highly competitive basis. A subcommittee consisting of executive committee members of ICME 2011 will review each application and make the selections.  Grants will be awarded to applicants who have a genuine need for support and a strong track-record on ICME related research, while paper quality and geographic distribution will also be taken into consideration. Applicants will be notified with the award decisions by Friday June 17, 2011.</p>
<p align="justify">ICME 2011 reserves the right to make the final decision regarding all applications.</p>

