<h1>Workshops</h1>

<h2>Call for Workshop Proposals</h2>

<p>ICME 2011, July 11-15, Barcelona, Spain</p>
<p>Download <a href="files/ICME2011_CFW0826.pdf" target="_blank"><u>PDF version</u></a> or <a href="files/ICME2011_CFW0903.txt" target="_blank"><u>TXT version</u></a> of this call for workshop proposals.</p>
<p align="justify">ICME Workshops  should be &ldquo;clearly focused&rdquo; and facilitating active discussions and  constructive interactions on current or emerging topics of interest. They should allow members of the community to compare and discuss approaches, methods, and new concepts on research topics pertinent to the main conference. These topics often offer emerging interests to the multimedia research community or bridge the community with other areas.</p>

<p>Workshop proposals for ICME 2011 should include:</p>
<ul>
  <li>The title, goals of the workshop;</li>
  <li>The specific topics that will be addressed by the workshop;</li>
  <li>The relevance and significance of this workshop to the main conference;</li>
  <li>The history of the  workshop if it is not the first version;</li>
  <li>A draft workshop call for papers with:</li>
  <ul>
    <li>The names, affiliations, addresses, phone numbers and email addresses of the workshop organizers, who should be experts in the related topics and preferably from multiple institutions;</li>
    <li>The paper selection process;</li>
    <li>A list of potential Program Committee members and  their affiliations.</li>
  </ul>
</ul>

<p align="justify">Workshop proposals should  not exceed 3 pages and should be submitted to the Workshop Chairs, <em>Abdulmotaleb El Saddik (elsaddik AT mcrlab.uottawa.ca)</em> and <em>Xian-Sheng Hua (xshua AT microsoft.com)</em>. Workshop proposals will be evaluated based on various criteria including the quality and level of details of the proposal, the theme of the workshop, its relevance to the main conference, and the overall balance of the conference workshop program.</p>

