<h1>CALL FOR WORKSHOPS: IEEE ICME 2015</h1>

<p>Download <a href="files/CFW_ICME2015.pdf" target="_blank"><u>PDF version</u></a> of this call for workshop proposals.</p>
<p align="justify">IEEE ICME 2015 will be held in Torino, Italy from June 29 to July 3, 2015. In addition to the main technical program and the exposition, the program will host a number of workshops. The goal of the workshops is to explore current and emerging research topics, and to provide a forum for authors to present early research results on these topics. The workshops should be focused and should allow members of the community to compare and discuss approaches, methods, and new concepts on research topics pertinent to the main conference. These topics often offer emerging interests to the multimedia research community or bridge the community with other areas. We aim to have a limited number of workshops, with a high quality program that consists mainly of contributed papers. The workshop organizers will have access to the CMT system for handling the review process, and the workshop papers (of length up to 6 pages) will be submitted for inclusion to IEEEXplore.</p>

<p>We seek workshop proposals from individuals and teams interested in organizing vivid and compelling workshops. The workshop proposal should provide the following information:</p>
<ul>
  <li>Title, scope, topics and duration (half or full day) of the workshop;</li>
  <li>Workshop format (papers, demos, panels, invited speakers, etc.);</li>
  <li>History of the workshop if any (where it was held, number of submissions, number of accepted papers, numbers of attendees in past years)</li>
   <li>Organizing team and tentative committee lists (organizers, steering committee if any, confirmed TPC members, endorsement by society TCs, etc.)</li>
<li>Workshop rationale:</li>
  <ul>
    <li>how the workshop is related to ICME;</li>
    <li>why the topic i timely and important;</li>
    <li>Why the workshop may attract a significant number of submissions of good quality;</li>
<li>Difference from other workshops and conferences with similar topic;</li>  </ul>
</ul>

<p align="justify">Please include a draft call for papers, if available.</p>

<p align="justify">Submission: Please send a whole copy of your proposal by 21 November 2014 in PDF format to <a href="mailto:icme2015workshops@gmail.com">icme2015workshops@gmail.com</a>.</p>

