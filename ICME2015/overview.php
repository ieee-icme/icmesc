<h2><a name="pangqwe"></a>I - Multimedia Graph-Based Processing</h2>
<h3 style="margin-left:0px">Date/Time: Tuesday, June 30, 2:00pm-2:40pm</h3>
<h3 style="margin-left:0px">Room: Cavour</h3>
<strong>Slide set</strong>:  <span class="indented highlighted"><a href="slides/Graph-ICME2015.pdf">PDF (46Mb)</a></span><br>
<p align="justify">The slides of the talk are now available <a href="http://lts4.epfl.ch/files/content/sites/lts4/files/frossard/talks/Graph-ICME2015.pdf">here</a>.</p>
<p align="justify">In many applications such as social, sensor, and neuronal networks or multimedia analytics, high-dimensional data naturally reside on the vertices of weighted graphs. The emerging field of signal processing on graphs merges algebraic and spectral graph theoretic concepts with computational harmonic analysis to process such signals on graphs. In this short overview, we outline the main challenges of the area, discuss different ways to define graph spectral domains, which are the analogs to the classical frequency domain, and highlight the importance of incorporating the irregular structures of graph data domains when processing signals on graphs. We then review methods to generalize fundamental operators such as filtering, translation, transforms and dictionaries to the graph setting. We finally show illustrative applications of this emerging framework to challenging multimedia processing problems.</p>

