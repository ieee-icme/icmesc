<h1>Registration Policies</h1>

<p align="justify">Please read carefully through the ICME 2015 registration policy before starting the ICME 2015 registration, as different registrations have different rules and benefits.</p>


<p align="justify">Anyone who wishes to attend any event of ICME 2015, including workshops, tutorials, main conference technical sessions, panel discussions, industry forums, demo sessions, industry forums, grand challenges, keynote and plenary sessions, and exhibitions must register to attend. There are three types of registrations available for the attendees: Conference registration, workshop-only registration and exhibitor registration.</p>


<p align="justify"> <ul>
<li>Each <strong>full conference registration</strong> includes: attendance for the 5 days + welcome reception + banquet + proceedings.</li>
<li>Each <strong>non-full conference registration</strong> does not include the banquet.</li>
<li>Each <strong>workshop-only registration</strong> includes: attendance to the two-day workshop sessions on Monday (June 29) and Friday (July 3) + welcome reception.</li>
<li><strong>Exhibitor registration</strong>: includes exhibition desk and all benefits of a full registration.</li>
</ul></p>

<p align="justify">All registrations include one electronic copy of conference and workshop proceedings.</p>

<p align="justify"><strong>Author Registration Policy</strong>: Each accepted main conference paper must have at least one author registered according to the following policy at a non-student and non-life-member rate, <strong>no later than Apr. 15, 2015</strong>. Each accepted workshop paper, grand challenge paper, EU project paper, or industrial forum paper must have at least one author registered according to the following policy at a non-student and non-life-member rate, <strong>no later than May 15, 2015</strong>.</p>


<p align="justify">Each non-student and non-life-member full conference registration can cover up to 2 papers of any kind (i.e., main conference papers, workshop papers, grand challenge papers, EU project papers, or industrial forum papers) from the registered author. For example, one full conference registration can cover: 2 main conference papers; or 1 main conference paper and 1 industrial forum paper; or 1 grand challenge paper and 1 workshop paper; etc. Note that a demo paper that is associated with a regular paper will be considered to be already covered by the corresponding accepted paper.</p>


<p align="justify">If workshop paper authors (without a main conference paper) would like to attend the full conference, they must pay the full conference registration fee, which will cover up to 2 workshop papers.</p>


<p align="justify">One workshop-only registration can cover only 1 workshop paper.</p>

<p align="justify">Student registrations cannot cover any paper that will be published in IEEE Xplore. However, student registrations may be used to cover the presentation of other paper categories in accordance with the relevant calls, including one demo paper, one accepted ongoing work item, or one accepted poster presentation of a recent journal paper.</p>

<p align="justify"><strong>Author No Show Policy</strong>. One of the co-authors (or a qualified substitute) of an accepted paper/demo must report to the session chair before the respective session starts according to the published schedule, and present the paper/demo in person at the conference/workshops site. Any paper accepted into the technical program, but not presented on-site will be automatically withdrawn from the conference/workshops and the proceedings. IEEE further reserves the right to exclude a paper from distribution after the conference (e.g. removal from IEEE Xplore) if the paper is not presented at the conference. </p>

<p align="justify">If you have any questions regarding the registration policy, please contact <a href="mailto:info.icme2015@gmail.com">info.icme2015@gmail.com</a>. For questions related to the payment of registrations, please contact <a href="mailto:congress@stilema-to.it">congress@stilema-to.it</a>.  </p>

