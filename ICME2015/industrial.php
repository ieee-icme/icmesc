<p align="justify">IEEE International Conference on Multimedia &amp; Expo (ICME) has been the flagship multimedia conference sponsored by four IEEE societies (Communications, Computer, Circuits and Systems; and Signal Processing) since 2000. The goal of the <strong>Industrial Program</strong> is to promote applied research and applications, as well as facilitate collaborations between industrial and academia members of the multimedia community.</p>

