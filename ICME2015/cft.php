<h1>Tutorials</h1>
<h2>Call for Tutorials</h2>
<p>ICME 2011, July 11-15, Barcelona, Spain</p>
<p>Download <a href="files/ICME2011_CFT0826.pdf" target="_blank"><u>PDF version</u></a> or <a href="files/ICME2011_CFT0903.txt" target="_blank"><u>TXT version</u></a> of this call for tutorials.</p>
<p align="justify">The ICME 2011 organizing committee invites proposals for tutorials, which are to take place July 11-14, 2011.&nbsp; In a change from previous years, the tutorials will be open to all registered conference attendees without an additional tutorial charge. We seek proposals across a wide range of topics and levels&mdash;ranging from fundamentals to the latest advances in hot topic areas. Topics of interest include, but are not limited to:</p>
<ul>
  <li>Speech, audio, image, video, text processing</li>
  <li>Signal processing for media integration</li>
  <li>3D visualization, animation and virtual reality</li>
  <li>Multi-modal multimedia computing systems and human-machine interaction</li>
  <li>Multimedia communications and networking</li>
  <li>Multimedia security and privacy</li>
  <li>Multimedia databases and digital libraries</li>
  <li>Multimedia applications and services</li>
  <li>Media content analysis and search</li>
  <li>Hardware and software for multimedia systems</li>
  <li>Multimedia standards and related issues</li>
  <li>Multimedia quality assessment</li>
</ul>

<h3>Proposal Format</h3>
<p><span class="indented">Each tutorial proposal must contain the following:</span></p>
<ol>
  <li>Title</li>
  <li>Names and Affiliations of Speakers</li>
  <li>Abstract (one or two paragraphs)</li>
  <li>Intended Audience (one paragraph): Describe the background assumed of tutorial attendees. Tutorials may range from basic for attendees new to the field to advanced topics for experts.</li>
  <li>Length: Half Day (to be scheduled on July 11) or One Hour (to be scheduled on July 12, 13, or 14).</li>
  <li>Description: A statement (no more than 3 pages) giving clear motivation/justification for the topic to be presented at ICME and a comprehensive outline of the proposed content.&nbsp; </li>
  <li>Materials: A description (one or two paragraphs) of materials to be provided to attendees on the conference DVD &ndash; course slides, annotated bibliography, code snippets etc. NOTE: the materials themselves do not need to be provided in the proposal.</li>
  <li>Biosketches: A half-page biosketch of each tutorial presenter must also be included.</li>
</ol>

<h3>Selection</h3>
<p align="justify"><span class="indented">All tutorial proposals will first receive peer review. Then, from the pool of proposal deemed of the highest quality, the chairs will make the final selection of tutorials to present the greatest variety to conference attendees and to fit the conference schedule.</span></p>

<h3>Upon Acceptance</h3>
<p align="justify"><span class="indented">Speakers for all accepted tutorials will be required to sign a commitment to present at the conference within 30 days of notification.</span></p>
<p align="justify"><span class="indented">Conference registration fees will be waived for speakers at accepted tutorials &ndash; one speaker for each one hour tutorial, up to three speakers for half day tutorials. The complimentary registration cannot be used to cover any papers accepted in the main program or workshops (that is, having a tutorial does not eliminate the need to have at least one paid registration for each paper at ICME 2011). Materials for the conference DVD must be delivered by April 1.</span></p>

<h3>Proposal Submission</h3>
<p><span class="indented">Tutorial proposals must be in PDF format and submitted via the following link:</span></p>
<p><span class="indented"><a href="https://cmt.research.microsoft.com/ICME2011/"><strong><u>Upload Proposal</u></strong></a></span></p>
<p align="justify"><span class="indented">Any questions should be addressed to the Tutorial Chairs: <em>Enrico Gobbetti (gobbetti@crs4.it)</em> and <em>Holly Rushmeier (holly.rushmeier@yale.edu)</em>.</span></p>
