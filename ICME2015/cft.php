<h2>Call for Tutorials</h2>
<p align="justify">The IEEE International Conference on Multimedia & Expo (ICME) has been the flagship multimedia conference sponsored by four IEEE societies since 2000. It serves as a forum to promote the exchange of the latest advances in multimedia technologies, systems, and applications from both the research and development perspectives of the circuits and systems, communications, computer, and signal processing communities. ICME 2015 will be held in Torino, Italy, from June 29 to July 3, 2015</p>
<p>Download <a href="files/ICME2015_CFT.pdf" target="_blank"><u>PDF version</u></a> of this call for tutorials.</p>
<p align="justify">The ICME 2015 Organizing Committee invites proposals for tutorials to be held on the first day of the conference Monday June 29, 2015. We seek half-day tutorials on established or emerging research topics within the field of multimedia and related topics, such as:</p>
<ul>
  <li>Multimedia content analysis</li>
<li>Multimedia activity and event understanding</li><li>Multimedia search and retrieval</li><li>Mobile, location-based and other context-based multimedia</li><li>Social, user-generated, and cloud-based multimedia</li><li>3D immersion and virtual reality</li><li>Multimedia security and forensics</li><li>Human computer interaction based on multimedia</li><li>Multimedia networking and communication</li><li>Multimedia coding and compression</li><li>Multimedia signal processing and enhancement</li><li>Multimedia systems, applications, services and implementations</li></ul>

